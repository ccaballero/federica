    <h2>Proyecto Vivo y Verde</h2>
    <p>Nuestra meta es la transmisión de conciencia ambiental entre el mARTadero
    y su público y barrio. Se busca un estado de funcionamiento dentro del
    mARTadero en que la mayoría de los desechos (agua y sólidos) sean
    reutilizados, y en el que la cantidad de basura —como tal— sea mucho menor.
    Esperamos que los vecinos vean que tan fácil es aprovechar nuestros
    residuos, y que implementen sistemas parecidos en sus propios hogares y
    casas. Adicionalmente, el mARTadero, siendo una gran presencia cultural en
    la cuidad, pueda actuar como ejemplo para otras instituciones o empresas.
    </p>
    <p>El mARTadero, con su alto flujo de público, ha comenzado un programa de
    manejo de basura, reciclaje y abono dentro su complejo que incluye: Las
    naves centrales, los callejones y patios internos, los espacios de talleres,
    de servicios y de administración, del ex matadero municipal de Cochabamba,
    así como la Plaza de Los Arrieros que es la entrada a este ex complejo
    industrial. Adicionalmente, el mARTadero busca instalar unos baños
    ecológicos, que utilizan procesos naturales de composteras para convertir
    los residuos sépticos en abono apto para huertos y jardines. Se utilizaría
    un modelo de baños ecológicos que ha sido desarrollado por nuestro
    cooperante, <a href="http://www.aguatuya.com/" target="_blank">Agua Tuya</a>,
    que consiste en un inodoro diseñado específicamente para la separación de
    residuos, dos tanques de compost, y una base para la estructura.</p>
