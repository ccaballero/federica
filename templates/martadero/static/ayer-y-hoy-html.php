<p>Tras 1992, lo que antes era un foco de infecciones, donde se sacrificaba la
vida, se convirtió provisionalmente, con ayuda de algunos pupitres, pizarras y
tizas, en escuelita, y algunas tardes a la semana en <strong>escuela de
deportes</strong>.</p>
<p>En vez de la mejora de dichos usos, el lugar iría siendo progresivamente
usado como <strong>depósito de alumbrado público y de mobiliario
municipal</strong> con desperfectos y en desuso, con la consiguiente degradación
provocada al inmueble. El canchón trasero, donde antaño estuvieran las reses, se
utilizaría como resguardo de bienes incautados por la ley 1008, convirtiéndose
en la práctica en depósito de chatarra. Así se lo fue dejando en abandono por
largo tiempo. Durante sus diversos usos siempre provisionales, el edificio fue
alterado con la introducción de paredes precarias tomadas con mortero de yeso, y
otras construcciones de poco valor arquitectónico, constructivo y documental.
Igualmente, desaparecerían las carpinterías, y parte de la maquinaria.</p>

<p>Tras un letargo de 12 años, en 2004 se retomaría la idea de los vecinos
realizando allí mismo el II Concurso Nacional Bienal de Arte Contemporáneo (II
conart 2004), que detonaría una serie de procesos para la progresiva
recuperación del sitio. La apertura y visión de la entonces Oficial Mayor de
Cultura, Jenny Rivero, junto al compromiso de un grupo de artistas liderados por
Angélika Heckl y Fernando García, posibilitarían impulsar el proyecto y la
solicitud de comodato al Concejo Municipal.</p>

<p><strong>El ex-matadero, de 2.900 m2 es</strong> -como consecuencia de todo
aquel proceso de diseño, construcción y uso- <strong>un espacio único,
elocuente, flexible, descentralizado, estratégico social y geográficamente,
acorde con la lógica y las necesidades de las artes emergentes y de una
interacción social positiva</strong>. Un lugar previamente abandonado que está
siendo recuperado, adecuado y puesto en valor para su uso permanente. Y ello en
Villa Coronilla, un barrio desfavorecido que conecta la pobre zona sur con el
centro donde vive gran parte de la clase media. Inmueble potencial y
presuntamente patrimonial, y <strong>situado en una interesante ubicación
"fronteriza" entre la realidad y el imaginario</strong> de ciudad consolidada y
los barrios periféricos receptores de inmigración en los últimos decenios.</p>

<h2>Estado del ex-matadero municipal en julio de 2004</h2>
<p>Cuando la asociación NADA comenzó trabajos en el antiguo matadero, los
distintos ambientes se encontraban en el estado que muestran las fotos. Al ser
usados como depósitos de muebles viejos y artefactos de alumbrado público, el
bien patrimonial estaba sometido a una notable degradación arquitectónica. Sin
embargo, los espacios mostraban enormes posibilidades para su recuperación y
puesta en juego como proyecto cultural.</p>
<p>La <strong>necesaria limpieza y despeje</strong> se realizó con la ayuda de
la Honorable Alcaldía Municipal de Cochabamba, coorganizadora del II CONART
2004, y las imágenes de la misma <strong>se registraron en un video documental
denominado "Matamorfosis"</strong>, filmado y editado por Celuloidea.
Afortunadamente, la solidez de la construcción del inmueble soportó los
distintos embates del tiempo y el mal uso del lugar, llegando a la actualidad
con una <strong>considerable integridad y resistencia</strong>.</p>
<p>Las distintas características espaciales de cada sala fueron motivos de un
relevamiento pormenorizado y de un proyecto de recuperación y cualificación
progresiva que permitiese la versatilidad de uso y la puesta en valor de las
características arquitectónicas y espaciales singulares.</p>
<p>Cinco años después, ciertas áreas del proyecto necesitan una intervención
integral, aunque <strong>más del 70% del lugar está ya adecuado para un uso
cultural intensivo</strong>.</p>

<h2>El II CONART 2004 como detonante<br />
(El arte contemporáneo en Bolivia y la formación de NADA)</h2>
<p>En el año 2004, un grupo de artistas contemporáneos, convocados y liderados
por Angelika Heckl, participamos en el <strong>II conart, concurso bienal de
arte contemporáneo</strong>.</p>

<p>El espacio elegido para la exposición de las obras iba a ser esta vez
descentralizado y sugerente: el ex matadero de la ciudad de Cochabamba, bien
municipal -y por tanto público pero en este caso de carácter privado- en estado
de abandono, situado en un barrio desaventajado y con características espaciales
ideales para los nuevos lenguajes y la filosofía que se encuentra detrás de
ellos. <strong>El arte contemporáneo se nos presentaba como un elemento clave de
integración y unión</strong>. Su propio y maravilloso carácter
<strong>interdisciplinar</strong> y la libertad que siempre propugna, casi
dejaba el terreno abonado para la necesaria interrelación que facilitase una
fecunda generación de propuestas. La <strong>interdependencia</strong> se iba
afianzando, mientras las prometedoras "huellas del futuro" iban dibujándose en
el Concurso y en el lugar, abriéndose camino entre las innumerables marcas del
pasado&hellip;</p>

<p>Cochabamba, en 2002 y con el <strong>I conart</strong>, sería la
<strong>primera ciudad del país</strong> en proponer una convocatoria
<strong>específicamente para el arte contemporáneo</strong>. El "centro
escéptico" que representa la ciudad -por su ubicación geográfica pero también
ideológica propia del carácter valluno- era ideal para inducir desde acá una
renovación conceptual que detonase los ansiados y necesarios procesos de
transformación artística que movimientos como "el ojo piensa" habían comenzado a
proponer años antes. El <strong>II conart 2004</strong> suponía la madurez de la
propuesta, capaz de renovarse y proponer un verdadero cambio.</p>

<p>Aquel año, y ya en el ex matadero, <strong>veinticinco artistas, con 29 obras
elegidas</strong> de entre 69, participarían del encuentro -el marcado con el
código 001 de los realizados en el lugar- ya dentro del marco de un proyecto que
estaba siendo delineado pero que aun no existía en la practica aunque se
"presentía": el<strong> proyecto mARTadero</strong>.</p>

<p>Así, las obras seleccionadas en aquel entonces fueron propuestas por artistas
de la talla de Erika Ewel, Raquel Schwartz, Eduardo Ribera, Valia Carvalho,
Patricio Crooker, Keiko Gonzalez, Vassil Anastasov, Alejandra Dorado, Equipo
Quipo, Alejandra Alarcón, Alejandra Delgado, José Ballivián, Sammy San Miguel,
Roger Jeri, Ivette Mercado, Tatiana Fernandez, Gonzalo Ordonez, Ana Vargas, Juan
Pablo Arze, Mariel Kunkar, La Caja Verde, Laura Martínez y Marcelo Santorelli,
Adriana Bravo, María Elisa Reyes, y el Colectivo Artístico de Identidad
Variable. Los invitados, nacionales y extranjeros, fueron: Joaquín Sánchez,
-ganador del primer conart-, Roberto Valcárcel, Gastón Ugalde, Rubén Castillo,
Laura Spivak, Spielhofer, Osvaldo Salerno, Gonzalo Rabanal, Eberhard Petschinka,
Vera Bourgeois, Jenny Jaramillo, Nora Iniesta, Guentzel &amp; Scheibe, Yan
Duyvendack, Gaspard Buma, San Keller, José Franco.</p>

<p>El jurado estaría formado por Ticio Escobar, Cecilia Bayá, y Ramiro Garavito,
mientras la curaduría la llevaría a cabo Angélika Heckl, asesorada por Harm Lux.
Dichos jurados otorgarían el PREMIO ÚNICO CONART 2004 a EQUIPO QUIPO por su obra
LEGIÓN, <em>"por la posibilidad de elaborar un planteamiento estrictamente
contemporáneo asumiendo formas y contenidos de la cultura popular
local"</em>.</p>

<p>Nueve países participarían y disfrutarían de la convocatoria, que supuso un
enorme éxito para la ciudad de Cochabamba, posicionándola dentro de un escenario
global. Porque, efectivamente, el panorama comenzaba a cambiar.
<strong>Cochabamba, durante ese 2004, sería el centro del arte contemporáneo en
Bolivia</strong>. Y el antiguo <strong>matadero municipal, a pesar de sus
limitaciones y gracias a sus posibilidades, tendría un papel fundamental en la
proyección</strong> de una visión diferente de arte y de gestión cultural.</p>

<p>La <em><a href="http://es.wikipedia.org/wiki/Glocalización" target="_blank">glocalización</a></em>
supuso entonces un fuerte ejercicio de voluntad, la opción y la decisión de
asumir nuestro propio eje de coordenadas, llevarlo dentro de nosotros mismos.
Conectarnos, desde lo micro, a todo lo macro, entendiendo el <strong>gran
sistema natural y social</strong> en el que estamos inmersos y del que formamos
parte. Nuestras acciones tomaban entonces una dimensión global y sugerente,
aunque estuviésemos lejos de los centros de poder, lejos de los circuitos
artísticos. Un espacio para georeferenciarnos creativamente.</p>
<p><strong>En Octubre se realiza exitosamente el II conart 2004</strong>,
demostrando el lugar sus <strong>óptimas condiciones</strong> para el arte
contemporáneo. En Diciembre, el lugar sería también sede del 8º Festival de
Teatro Bertolt Brecht, mostrando igualmente su adecuación para las artes
escénicas. <strong>El arte iba haciendo revivir al
matadero&hellip;</strong></p>
