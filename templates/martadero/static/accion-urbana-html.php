<h1>Acción urbana</h1>

<p>El programa, que cuenta con el apoyo de
<a href="http://www.emprendeduriacultural.net/ejecentral/">Eje Centr4l</a>,
consiste en el desarrollo de proyectos y propuestas para la
<strong>generación de un proceso de participación ciudadana que impulse la
rehabilitación, resignificación y acondicionamiento de la Plaza de los Arrieros
de Cochabamba (Bolivia), para convertirla en la</strong>
<a href="<?php echo $this->url(array('page' => 'paseo-de-las-artes.html'), 'base_static') ?>#plaza">Plaza
de las Artes</a><strong> -y por extensión, en el</strong>
<a href="<?php echo $this->url(array('page' => 'paseo-de-las-artes.html'), 'base_static') ?>">Paseo
de las Artes</a> en los ejes determinados-.</p>

<p>Son <strong>espacios destinados a ser un punto de encuentro y de desarrollo
de la creatividad</strong> para la gente en donde se combine recreación,
diversión, ocio y descanso. Este proceso busca generar la reflexión y la
concienciación en los vecinos del barrio con la finalidad de que lleguen a un
progresivo mejoramiento de su contexto vital.</p>

<h2>Objetivo general:</h2>
<p>Generar un espacio público plural, donde las manifestaciones de diversidad
sean un elemento enriquecedor y no un factor excluyente.</p>

<h2>Propósito:</h2>
<p>Reconstruir un sentido de pertenencia comunitario y responsabilidad colectiva
en el barrio de Villa Coronilla impulsando el encuentro y el intercambio de
ideas entre los vecinos.</p>

<h2>Objetivos Específicos:</h2>
<ol>
    <li>Generar un sistema constante de recogida de la memoria histórica del
    barrio.</li>
    <li>Activar un flujo masivo de comunicación entre vecinos sobre los
    problemas del barrio.</li>
    <li>Impulsar el nacimiento de una estructura que permita la construcción
    colectiva de modelos de futuro por parte de los vecinos, como plataforma
    material y organizativa para activar el diálogo y el intercambio de ideas
    entre los habitantes del barrio de Villa Coronilla.</li>
</ol>

