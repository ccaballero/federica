<h2>ANTECEDENTES</h2>
<p><strong>El mARTadero</strong>, como proyecto integral de desarrollo humano y
social a través del arte y la cultura, <strong>cuenta con un laboratorio digital
bien equipado, fruto del trabajo conjunto entre la artista Lucía Grossberger
Morales, la Fundación Imagen, y la Asociación NADA</strong>, con el apoyo de
CUSO, MCIC, y otros colaboradores. El deseo para dicho espacio fue siempre el
de <strong>convertirlo en una plataforma para la creación y para la
formación</strong>.</p>
<p>La <strong>toma de conciencia sobre los problemas que aquejan nuestras
sociedades</strong>, el <strong>necesario enfoque interdisciplinario para
encararlos y resolverlos</strong>, y la <strong>certeza del inmenso poder de la
creatividad</strong> para transformar estructuras, han sido siempre
<strong>temas transversales del proyecto mARTadero</strong> desde sus
inicios.</p>
<p>Por ello, <strong>reafirmamos nuestra convicción en la importancia de la idea
y del concepto</strong>, y en la utilidad -<strong>como herramienta
expresiva</strong>- de la tecnología y de los otros medios y lenguajes
contemporáneos para desplegar la condición de agentes de cambio que los artistas
han de tener.</p>

<p>El <a href="<?php // TODO ?>index.php?page=formarte">programa formARTe</a>
lleva ya impartidos exitosamente decenas talleres, con cientos de participantes
que han recibido valiosos insumos para la creación en las distintas artes.</p>

<p>proyectARTe supone una ampliación del programa formARTe, que implica
procesos de mayor duración en el tiempo y con mayor proyección en cuanto a
alcances.</p>

<h2>PROPUESTA</h2>
<p><strong>La toma de conciencia política y la expresión de nuestra mirada como
artistas ante la realidad en que desarrollamos nuestra actividad motivan la
presente convocatoria</strong>, primera de una serie que se irá desarrollando en
el arco de los próximos años.</p>

<p>En <strong>Septiembre de 2009 se realizará una exposición conjunta en Tucson
(Arizona) y Cochabamba (Bolivia)</strong>, y posteriormente en <strong>otros
espacios y medios de USA, Canadá y Bolivia</strong>, con las obras de unos doce
artistas digitales que hayan participado del proceso.</p>

<p>Se busca <strong>abrir un espacio de discusión y creación digital enfocado a
la generación de productos de calidad que puedan ser compatibles
internacionalmente y representar a Bolivia en espacios artísticos del
exterior</strong>.</p>

<p>Dichos productos no serán documentales, sino una <strong>propuesta de
carácter artístico y expresivo</strong>, proveniente de la propia creatividad.
Deberán, pues, manejar las lógicas propias del arte, en cuanto a inclusión
conceptual y exclusión formal, manejo poético y metafórico, referencialidad,
etc&hellip;</p>

<p><strong>Se proponen limitaciones que han de servir como alicientes a la
creatividad, no como límites de la misma</strong>.</p>

<p>El programa tendrá en esta primera etapa una duración de 4 meses (8 sesiones
de trabajo). Tras esta primera convocatoria, se evaluarán los resultados y se
decidirá sobre su posible continuación con versiones sucesivas.</p>

<h2>CONVOCATORIA 2009</h2>
<p>Se invita, pues, a los artistas interesados a <strong>producir una obra
digital encontrando metáforas en las limitaciones propuestas y mediante la
participación en un proceso formativo-creativo</strong> que desenvocará en una
muestra internacional de los trabajos.</p>

<p>Dichas obras digitales tendrán las <strong>siguientes
características</strong>:</p>

<ul>
    <li>Una <strong>presentación en Power Point de máximo tres minutos</strong>,
    teniendo en cuenta que se podrán usar en ambos casos otros programas de
    apoyo para cualificar la propuesta. Posteriormente se editarán los afiches y
    los power point (convenientemente pasados a quick time) en un DVD
    conjunto.</li>
    <li>Parte del trabajo será <strong>investigar todas las posibilidades
    técnicas y expresivas que el programa da</strong>. Se deberán usar al menos
    dos recursos de entre los siguientes: Texto, imagen estática, imagen en
    movimiento, música, voz incorporada.</p>
    <li>El presente programa de formación <strong>está subvencionado</strong>,
    por lo que no implica costo para los participantes, pero implica:
    <ol>
        <li>La redacción de una carta de intenciones, sobre porqué le interesa
        el programa, y porqué quisiera trabajar sobre una temática con enfoque
        político.</li>
        <li>El compromiso de continuidad durante el periodo de producción (al
        menos 2 mañanas de sábado al mes durante 4 meses, con una falta tolerada
        del 15% máximo). Supone, pues, al menos 32 horas de trabajo, en 8
        sesiones de las que se puede faltar, como máximo, 1.</li>
        <li>Cada persona trabajará en un computador, por lo que se solicitará en
        la solicitud que se especifique si cuentan con una computadora portátil
        para disponer en esos períodos, pudiendo así aumentarse el número de
        admitidos.</li>
        <li>Se realizarán conexiones -cuando sea necesario- via skype con Tucson
        (con Lucía Grossberger Morales) y otros lugares, mediante monitor,
        webcam y micrófono ambiente, para el seguimiento por parte de los
        facilitadores desplazados.</li>
        <li>La semana consecutiva a la sesión de trabajo, y hasta el día sábado
        de la misma (sábado alterno), se deberá mandar el trabajo en curso a los
        facilitadores, para sus observaciones y correcciones.</li>
    </ol>
    </li>
    <li>El programa facilitará, gestionará y propondrá -bajo el monitoreo de los
    facilitadores y para el mejor desarrollo de los participantes y calidad de
    los productos artísticos- las siguientes facilidades:</li>
    <li>Medios tecnológicos suficientes, y conexiones a Internet de banda ancha
    (1024 kbps) y wi-fi.</li>
    <li>Un espacio exclusivo para el trabajo grupal los sábados por la mañana,
    en el laboratorio digital del proyecto mARTadero, y facilidades de uso en
    otros momentos previo aviso.</li>
    <li>Encuentros puntuales con expertos en temáticas de realidad nacional, y
    artistas con posicionamientos claros frente a la problemática nacional. Los
    <strong>expertos inicialmente identificados</strong>, y los temas que
    tratarán, son:
    <ol>
        <li>Alejandra Dorado: Trabajos con collage digital. Ejemplos, ideas y
        consejos.</li>
        <li>Ramiro Garavito: El compromiso político del artista.</li>
        <li>Angelika Heckl: La función del arte en la sociedad actual.</li>
        <li>Arturo Von Vacano: Bolivia hoy. Luces y Sombras.</li>
        <li>Juan Malebrán: Las palabras y las cosas.</li>
        <li>Alberto Villalpando: La música de la vida.</li>
        <li>Gonzalo Ordóñez: Marketing cultural de una obra.</li>
        <li>Fernando García: Arte y creatividad entre poder o comunicación.</li>
    </ol>
    </li>
</ul>

