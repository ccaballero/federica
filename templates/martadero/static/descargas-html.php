<h3>Sinopsis de la obra</h3>
<p>El proyecto mARTadero -de Cochabamba, Bolivia- es un proyecto integral y
multidimensional concebido como vivero de las artes y enfocado al cambio social
responsable mediante mecanismos artístico-culturales. Tiene vocación de
prototipo y de foco irradiador de creatividad y mejoras para el entorno, y
realiza para ello -partiendo de una cooperación entre artistas bien organizada y
estructurada en siete áreas, y de unos principios claros de acción- un amplio
espectrum de actividades artístico-culturales.</p>
<p>El presente trabajo intenta identificar las claves del éxito del proyecto, de
su rápido crecimiento y proyección, a través del análisis de tres tiempo:</p>
<p><strong>el pasado</strong>, que nos lleva a entender la época de prosperidad
en que se construye el matadero, el significado histórico de la zona donde se
ubica, la personalidad del constructor, y las características formales y
tipológicas del inmueble,</p>
<p><strong>el presente</strong>, donde se reflexiona sobre el origen del
proyecto cultural y su conexión con el arte contemporáneo, la fuerza de la
visión proyectada, la comprensión dinámica del carácter patrimonial del lugar, y
la oferta actual del sitio,</p>
<p>y <strong>el futuro,</strong> en el que se discuten temas esenciales como la
necesaria corresponsabilidad social, las articulaciones y las redes
territoriales, las estrategias básicas a seguir, y el carácter del proyecto como
propuesta artística y work in progress.</p>
<p>Represente por ello un valioso registro de los primeros cuatro años de vida y
actividad, mostrando parte de las investigaciones en curso alrededor de su
desenvolvimiento, y ofreciendo ciertas pautas y documentos referenciales para
emprendimientos similares.</p>
<p><strong>Si quieres adquirir el libro impreso</strong> puedes hacerlo en la
sede del proyecto mARTadero, en la calle 27 de Agosto con Ollantay, Cochabamba,
Bolivia.</p>
<p>Además, <strong>todas las ganancias generadas por la venta del libro van
destinadas a un Fondo Editorial</strong>, el cual financiará la publicación de
otras obras que traten el tema del arte, desarrollo social e investigaciones
sobre la zona y el proyecto.</p>
