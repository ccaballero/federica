<h1>Políticas Culturales en Bolivia</h1>

<p><strong>Desde el mARTadero</strong> queremos <strong>impulsar el avance de
Políticas Culturales en nuestro país</strong>.</p>
<p>La posibilidad de abrir el <strong>debate a través de plataformas de
comunicación</strong> se presenta como una <strong>necesidad de las sociedades
democráticas</strong> cotemporáneas, y así, con la <strong>creación de este
blog</strong>, <strong>símbolo de reciprocidad y espacio abierto de intercambio
de opiniones</strong>, esperamos accesibilizar este debate.</p>

<p>Este blog tiene la intención de constituirse en un <strong>aporte de Proyecto
mARTadero al proceso actual de gestación de una política cultural</strong> para
nuestro país.</p>

<p>En pocas palabras, la <strong>política cultural</strong> se entiende como
<em>aquella área de legislación pública que rige las actividades relacionadas a
las artes y la cultura e involucra, por lo general, la gestación de procesos,
clasificaciones legales e instituciones que promuevan la diversidad cultural y
la accesibilidad, así como la promoción y difusión de las expresiones
artísticas, sociolingüísticas, y literarias de todos los pueblos.</em></p>

