<h3>ALIVE AND GREEN</h3>
<p><img src="<?php echo $this->staticUrl('/vivo-y-verde.jpg') ?>" title="mARTadero alive and green" /></p>
<p>The main objective of this program is to transmit an environmental awareness
to the people in the neighborhood and to those visiting us. The premise is to
reuse waste (water and solids) and to reduce the amount of garbage. MARTadero
has different bins to separate waste according to its composition (plastics,
organic waste, compost, glass etc), and we want this to serve as an example that
can be replicated at home. Likewise, we are going to install composting toilets
that turn septic waste into fertilizer for gardens and orchards through a
natural process.</p>
