<h2><a name="principios"></a>7 PRINCIPLES</h2>
<p>The criteria that we apply to all our proposals are based on the same
principles enshrined in NADA&rsquo;s rules of association, which are the 3 main
pillars of the project:</p>

<h3>INNOVATION</h3>
<p><img src="<?php echo $this->staticUrl('/innovacion.jpg') ?>" title="mARTadero innovation" /></p>
<p>An innovative proposal is one in which conceptual contents go beyond what is
given or established and which setting or formal aspects are coherent with the
uniqueness of the content.</p>

<h3>RESEARCH</h3>
<p><img src="<?php echo $this->staticUrl('/investigacion.jpg') ?>" title="mARTadero research" /></p>
<p>The proposal must be the result of research and consideration of the contents
and their social and cultural implications.</p>

<h3>EXPERIMENTING</h3>
<p><img src="<?php echo $this->staticUrl('/experimentacion.jpg') ?>" title="mARTadero experimenting" /></p>
<p>A proposal is experimental when the contents, and thus the implementation,
question in a responsible way the limits of what is artistically or esthetically
established.</p>

<h3>CONCEPTUAL AND FORMAL STRINGENCY</h3>
<p><img src="<?php echo $this->staticUrl('/rigor.jpg') ?>" title="mARTadero conceptual and formal stringency" /></p>
<p>The conceptual and formal stringency of a proposal refers both to the logical
coherence of its conceptual approach as well as to the accomplishment of its
implementation.</p>

<h3>INTEGRATION</h3>
<p><img src="<?php echo $this->staticUrl('/integracion.jpg') ?>" title="mARTadero integration" /></p>
<p>Integration is a broad concept  that involves the capacity of integrating
artistic disciplines or languages, as well as the need for social and cultural
integration from a plural perspective.</p>

<h3>KNOWLEDGE AND EXPERIENCE EXCHANGE</h3>
<p><img src="<?php echo $this->staticUrl('/intercambio.jpg') ?>" title="mARTadero exchange" /></p>
<p>It refers to the need for a proposal to have a knowledge-based impact on
society, in such a way that it can generate knowledge rather than the
accumulation of information. It refers to the critical capacity arising from an
integrating, communal, dialogic and dialectic knowledge.</p>

<h3>INTERCULTURALITY</h3>
<p><img src="<?php echo $this->staticUrl('/interculturalidad.jpg') ?>" title="mARTadero interculturality" /></p>
<p>Interculturality means not only to respect cultural diversity but to enhance,
know and value its artistic and cultural manifestations.</p>
<p>We are convinced that when art and culture are alive and shared, this has an
essential impact on development and social wellbeing. That is why we consider it
a priority to create a broad and inclusive (physically and conceptually) space
for creation.</p>

<h2>Our space must therefore serve the purpose of:</h2>
<ul>
    <li>Synergistic creation and multidisciplinary innovation in arts and
    culture.</li>
    <li>Visibility and accessibility of emerging culture offerings.</li>
    <li>Research and implementation of new culture management models.</li>
    <li>Social interaction and integration through art.</li>
    <li>Going beyond national limits and boundaries towards a true
    interculturality based on dynamic and inclusive identities.</li>
    <li>Identifying leaders that enable the emergence of processes.</li>
    <li>Generating flows and networking.</li>
</ul>
