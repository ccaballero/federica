<h3>CULTURAL POLICIES AND NETWORKS</h3>
<p><img src="<?php echo $this->staticUrl('/politicas-culturales.jpg') ?>" title="mARTadero cultural policies" /></p>
<p>Cultural policies are understood as "the area of public policy-making that
governs activities related to the arts and culture. Generally, this involves
fostering processes, legal classifications and institutions which promote
cultural diversity and accessibility, as well as enhancing and circulating the
artistic, sociolinguistic and literary expressions of all people".</p>
<p>This program aims at enhancing the advancement of Cultural Policy in our
country, since the possibility for opening up the debate through communication
platforms arises as a need of contemporary democratic societies. This is why we
are hoping to enable this debate through a blog, a symbol of reciprocity and
opinion-exchange open spaces. We want this to be the contribution of the
mARTadero Project to the current gestation of a cultural policy for our country.
We thus try to promote three specific, high priority laws: one on Culture
Centers, another on Cultural Spaces and a last one linked to Artistic Promotion
Grant Funds.</p>
