<h3>ARTISTIC AND CULTURAL BUSINESS INCUBATOR</h3>
<p><img src="<?php echo $this->staticUrl('/vivero.jpg') ?>" title="mARTadero incubator" /></p>
<p>One of the main problems of the culture and arts sector is that it does not
rely on necessary expertise for advancing in the business world nor for
establishing legal institutions. Likewise, it lacks an adequate organizational
structure to establish market relations at a national and international level.
This is the reason why we are proposing -together with Fundación Imagen- a
project that supports enterprising artists, allowing them to use their
creativity to set a comprehensive strategy that contributes to economic and
social development in Bolivia. The main objective is to get to provide those
interested in artistic jobs with an environment equipped with the necessary
infrastructures, marketing advice, sales and trading, business tutors or mentors
and management follow-up support, among other things.</p>
<p>The objective of this program is:</p>

<ul>
    <li>To promote the economic development of the arts sector, contributing to
    the organization of the culture and creativity sector and to its recognition
    as a field with an increasingly important role in the world's economic
    dynamics.</li>
    <li>To entitle artists to start artistic and creative business, and to
    strengthen the very few existing ones, providing a space with technical and
    logistical equipment for them to consolidate their work in a professional
    way, thus strengthening quality cultural offerings.</li>
    <li>Providing the necessary tools regarding the different management areas,
    business management and new technologies in the culture and arts field.</li>
</ul>

