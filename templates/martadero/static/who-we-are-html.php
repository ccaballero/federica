<h3>PRANA ART RESIDENCIES</h3>
<p><img src="<?php echo $this->staticUrl('/prana.jpg') ?>" title="Prana mARTadero" /></p>
<p><strong>PRANA</strong> is a Sanskrit word that refers to "what is vital",
<strong>to the strength of living things and to the generation of energy through
the natural process of the Universe</strong>. It comes from the Upanishads and
it talks about that part of physical reality that supports the body, and is the
origin of mind and thought.</p>
<p>PRANA (Program of Artistic Residencies of NADA) is a program based on the
rooms built in our space, proposed by Fundación Imagen and with the
collaboration of the Embassy of the Netherlands. Resident artists and cultural
managers have one to four weeks (in the case of national artists) and four to
eight weeks (international artists) to produce a work. The proposal -either
physical or process-based- must contribute to the neighborhood of Villa
Coronilla or the city of Cochabamba, searching social interaction between
citizens and the artist's work.</p>
<p>The idea is to conceive artistic projects pertinent to the place, that can be
carried out during the stay and that incorporate the context, allowing
interaction with it.</p>
<p>The receiving center, through foundations, will take care of raising the
funds for the material and the equipment necessary for producing the work.</p>

<ul>
    <li>There is a top capacity of 8 persons (4 in each room) in PRANA-North and
    6 in PRANA-South, which makes a total of 14.</li>
    <li>Artists and managers who come with residency programs will have priority
    over other guests.</li>
    <li>Artists interested in staying as independents, outside of residency
    programs, will also have the possibility of doing so.</li>
</ul>

