<p>mARTadero is a comprehensive, social development project operating through
art and culture.</p>

<p>As a comprehensive project for the development of arts and culture towards
social change, <strong>we work for social development through art and culture
</strong>, doing so through <strong><a href="#areas">7 areas of artistic
creation</a></strong> and <strong><a href="#programas">7 social development
programs</a></strong>, both based on <strong><a href="#principios">7
principles</a></strong>. Each of the strategies we implement is in turn based
on 3 pillars:</p>

<ul>
    <li><strong>Space:</strong> the architectural space of the former
    slaughterhouse: 3,000 m² built in 1924, with its clear patrimonial character
    and awaiting declaration as heritage, expresses the yearning for progress
    which is typical of turn of the century industrial architecture. A unique,
    flexible and well-defined space which is decentralized, socially and
    geographically strategic, and in keeping with the logic and the needs of
    emerging arts. It has been lent for use for 30 years to NADA for the
    self-managed development of the project.</li>
    <li><strong>Cultural management:</strong> a multidisciplinary and open
    professional team with a deep command of intercultural promotion in the
    local, national and international sphere. An autonomous group working on an
    assembly basis; well-structured, focused on obtaining results that are clear
    and pertinent for society; capable of renewing itself and adjusting to
    contemporary standards; convinced of the advantages of staying connected,
    networking and reaching a consensus regarding the Culture of the Future.
    </li>
    <li><strong>The principles:</strong> seven maxims founded upon the clear
    objectives of innovation, research, experimentation; proposing conceptual
    and formal stringency, integration, exchange and interculturality as the
    criteria to be applied to all the proposals that are generated, supported or
    organized by the project. These principles convey the project's core values
    and communicate our belief in the potential for creating a better future.
    </li>
</ul>

<h2><a name="areas"></a>7 ARTISTIC CREATION AREAS</h2>
<p>During the six years of the mARTadero Project's existence, we have carried
out close to 800 different activities involving over 200,000 participants.
Today, mARTadero is the most innovative and fastest growing cultural project in
Bolivia and serves as an exemplary project in the region, thanks to the work of
over thirty people that have during this time contributed with their effort and
their capacity for management. An important part of this achievement is the
'seven artistic areas', the aim of which is to enhance and develop their area of
activity in the city of Cochabamba not only through exhibitions but also by
activating training and development mechanisms with a special penchant for
emerging arts. Please find below the specific objectives of each area:</p>
<p><img src="<?php echo $this->staticUrl('/estrella-areas.jpg') ?>" title="Artistic creation areas" /></p>

<h3>VISUAL ARTS AND PHOTOGRAPHY</h3>
<p><img src="<?php echo $this->staticUrl('/artes-visuales.jpg') ?>" title="mARTadero visual arts" /></p>
<p>The aim of this area is to position contemporary visual creations in the
Bolivian and international scenes through exhibits, events, exchanges,
participation in international networks, training workshops, support of Bolivian
artists abroad etc. The position of art as a space for social construction
through a multidimensional deployment of creativity in the context is a core
theme.</p>

<h3>LITERATURE</h3>
<p><img src="<?php echo $this->staticUrl('/letras.jpg') ?>" title="mARTadero literature" /></p>
<p>In the Literature area we try to promote the creation, circulation and
exchange of literary proposals through workshops, production and participation
in writing-related activities, both at national and international level. Also, a
special focus is given to knowledge acquired through individual and collective
experience in order to foster new voices in poetry and prose that are more
aware, critical and proactive regarding themselves and the context they live
in.</p>

<h3>PERFORMING ARTS</h3>
<p><img src="<?php echo $this->staticUrl('/artes-escenicas.jpg') ?>" title="mARTadero performing arts" /></p>
<p>This area seeks to enhance the training of Bolivian creators thanks to the
production of activities linked to the different trends and genres that make up
performing arts (Theater, Dance, Puppetry etc.) We also look after the
consideration of context in these expressions as a source of creation and social
construction. The activities are assessed and carried out in correspondence with
the guiding principles for all projects developing in mARTadero.</p>

<h3>GRAPHIC DESING AND ARCHITECTURE</h3>
<p><img src="<?php echo $this->staticUrl('/arquitectura.jpg') ?>" title="mARTadero architecture" /></p>
<p>In this area we try to contribute to the development of graphic design and
architecture in Cochabamba and Bolivia thanks to an interdisciplinary approach
at different levels through training, projects, meetings, networking and
significant interventions. Likewise, we guided the progressive recovery of the
old slaughterhouse, revitalizing its heritage essence and refurbishing the
different spaces for cultural and artistic use by the city. The same thing has
been done to other historical buildings, always taking into account the
environmental and bioclimatic dimensions of architecture.</p>

<h3>MEDIA</h3>
<p><img src="<?php echo $this->staticUrl('/audiovisual.jpg') ?>" title="mARTadero media" /></p>
<p>This area aims at encouraging experimental and professional media production
in our environment; promoting and producing creative, educative activities and
exhibitions of diverse genres and trends. We pledge our commitment to an
interdisciplinary approach to art; to exploring audiovisual language and to
designing an alternative production rationale in line with our economic and
cultural context.</p>

<h3>MUSIC</h3>
<p><img src="<?php echo $this->staticUrl('/musica.jpg') ?>" title="mARTadero music" /></p>
<p>In this area we want to facilitate, promote and disseminate the production of
music events (concerts, workshops etc) without discrimination against any
musical genre and with an interdisciplinary approach. We prioritize and focus
our work on musical creations by new artists, especially those who experiment in
composition and who, for diverse reasons, do not have access to spaces where
they can develop and express their art and talent.</p>

<h3>SOCIAL INTERACTION</h3>
<p><img src="<?php echo $this->staticUrl('interaccion-social.jpg') ?>" title="mARTadero social interaction" /></p>
<p>This area tries to enhance social development processes through art and
culture strategies, generating a progressive future-orientated movement based on
creativity and in the crucial role of culture as a facilitator for inclusion,
environmental awareness, economic production and social cohesion. Also, we look
for interaction between artists and the general public, paying special attention
to the new generations that are the true builders of a culture of future.</p>

<h2><a name="programas"></a>7 SOCIAL DEVELOPMENT PROGRAMS</h2>
<p><img src="<?php echo $this->staticUrl('/programas-desarrollo.jpg') ?>" title="Social development programs" /></p>

<h3>FREE EXPRESSION AND ENVIRONMENTAL EDUCATION CHILDREN’S WORKSHOP</h3>
<p><img src="<?php echo $this->staticUrl('/taller-infantil.jpg') ?>" title="mARTadero children's workshop" /></p>
<p>The Workshop is an open space where proposals can be made and whose objective
is for children to be able to freely explore their creativity through games,
handicrafts of all types, drawings and building blocks. We want them to find a
project to carry out under the supervision of the team of instructors, who are
in charge of managing the group according to its needs. Socializing while having
fun, meeting others, recycling, reusing and other dimensions of this workshop
allow children to live art as a means to assimilate the world that surrounds
them.</p>
<p><a href="<?php // TODO ?>http://www.martadero.org/programas/tallerninos.html">http://www.martadero.org/programas/tallerninos.html</a></p>

<h3>FORMARTE</h3>
<p><img src="<?php echo $this->staticUrl('/formarte.jpg') ?>" title="mARTadero formARTe" /></p>
<p>This is a program and a space for complementary, high quality and intensive
artistic training at an affordable cost aimed mostly at young people. Its main
objective is to serve as a knowledge exchange forum where social, personal and
cultural change can happen from and through art. It is self-financed thanks to
the fee paid by the participants –a minimum of 8-. There are full and partial
grants for the economically disadvantaged.</p>
<p><a href="<?php // TODO ?>http://www.martadero.org/programas/formarte.html%20">http://www.martadero.org/programas/formarte.html</a></p>

<h3>URBAN ACTION (ART SQUARE AND ART WALK)</h3>
<p><img src="<?php echo $this->staticUrl('/accion-urbana.jpg') ?>" title="mARTadero urban action" /></p>
<p>The Project, supported by Eje Centr4l, is based on the sparking off of a
process of citizen participation that promotes renovation, refurbishing and the
search for new meaning for the Arrieros de Cochabamba Square (Bolivia); to turn
it into the Arts Square and, by extension, the Art Walk that spans a specific
area. These spaces are intended to serve as a meeting point where people's
creativity can be developed, where recreation, fun, leisure and rest can be
combined. The process aims at promoting reflection and awareness among the
residents; to aspire to a gradual improvement in the context of their living
situation.</p>

<h4>Mission:</h4>
<p>Creating a multifaceted public space where signs of diversity are an element
of richness and not a factor of exclusion.</p>
<h4>Purpose:</h4>
<p>Rebuilding a feeling of community, belonging and collective responsibility in
the neighborhood of Villa Coronilla, facilitating the encounter and the exchange
of ideas among neighbors.</p>
<h4>Specific objectives:</h4>
<ul>
    <li>Create a permanent mechanism for amassing the history of the
    neighborhood.</li>
    <li>Activate a massive flow of communication among neighbors regarding the
    neighborhood's issues.</li>
    <li>Facilitate the creation of a structure that allows the neighbors to
    collectively build models for the future, as a material and organizational
    platform to spark off dialogue and the exchange of ideas among the
    inhabitants of the neighborhood of Villa Coronilla.</li>
</ul>

