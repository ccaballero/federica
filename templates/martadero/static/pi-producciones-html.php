<h2><a name="pilares"></a>NUESTROS PILARES</h2>

<p><a href="http://pi.martadero.org" target="_blank">&pi; Producciones</a> está
concebida como un instrumento del Proyecto mARTadero para generar un cambio
social por medio del arte y la cultura. Este cambio social será posible siempre
que se apueste por el desarrollo humano así como el desarrollo cultural.</p>
<p>Su trabajo está guiado por los <strong>7 principios del mARTadero</strong>,
los cuales son <strong>innovación, investigación, experimentación, rigor
conceptual y formal, integración, intercambio e interculturalidad</strong>,
siendo estos los criterios aplicados en nuestra propuesta de producción
artística y nuestra labor a favor de la cultura.</p>

<p><a href="<?php echo $this->staticUrl('/pi-grabacion.jpg') ?>" class="highslide" onclick="return hs.expand(this);">
<img src="<?php echo $this->staticUrl('/pi-grabacion-small.jpg' ) ?>" /></a></p>

<p><strong>Nuestra empresa se funda a partir de 3 pilares</strong>: la
<strong>gestión cultural</strong>, la <strong>integralidad</strong> y
<strong>diversidad</strong> de sus funciones y su infraestructura.</p>

<h3>Gestión Cultural</h3>
<p>Somos una entidad <strong>proactiva y propositiva</strong> que no espera que
los artistas se acerquen para trabajar, sino que <strong>concebimos y
gestionamos nuestros propios proyectos</strong>, los cuales a la vez de generar
ingresos para sostenernos, tiene un impacto efectivo en la sociedad.</p>
<p>Nos diferenciamos de otras productoras por esto, y por <strong>no enfocar
nuestra labor al mero lucro</strong>. Esto es lo que nos permite alejarnos del
esquema comercial relacionado con la producción de experiencias artísticas
superficiales pero altamente redituables (enlatados).</p>
<p>Para lograrlo, <strong>la productora combina su talento humano con el equipo
de gestión</strong> de las áreas Música y Audiovisual, así como del mARTadero en
su totalidad. A su vez <strong>se potencia con el Departamento de
Comunicación</strong> y trabaja en conjunto con programas como
<a href="<?php // TODO ?>{$path}programas/formarte.html" target="_blank">formARTe</a>
y Vivero de las Artes, etc.</p>
<p>Nuestros <strong>ejes de trabajo</strong> son: <strong>el rescate del
patrimonio</strong> proponiendo alternativas para el registro sonoro y
audiovisual de las distintas manifestaciones que existen en las áreas rural y
urbana, <strong>la creación</strong> concretada por medio de actividades de
experimentación (Taller de Creación Musical), jammings, encuentros musicales,
composiciones y arreglos musicales, <strong>la promoción artística</strong>
organizando actividades tales como encuentros, festivales, conciertos,
convocatorias, concursos, audiciones, etc., cuyos objetivos sean interactuar de
manera directa con los artistas, y promocionar su trabajo.</p>

<h3>Integralidad y variedad de servicios</h3>
<p>La producción artística es una labor que debe adaptarse a las exigencias del
cliente quien opta por uno u otro servicio según sus necesidades y su
disponibilidad de recursos económicos.</p>
<p>La productora oferta una gran variedad de servicios derivados de la
combinación de las tareas de dirección artística, registro sonoro y audiovisual.
A su vez combina su trabajo al del mARTadero para incluir los servicios de
organización de eventos y diseño gráfico. De estos emanan una serie de combos,
adaptados a los requerimientos del mercado.</p>

<h3>Infraestructura y equipamiento</h3>
<p>La infraestructura con la que se cuenta es <strong>completamente
inédita</strong>. Combina <strong>lo mejor de un estudio de grabación
profesional con un envidiable soporte audiovisual</strong>. Sin embargo,
<strong>nuestra principal oferta es la realización grabaciones de conciertos en
vivo</strong>. El estudio de grabación está instalado al lado del
<a href="<?php // TODO ?>{$path}archivos_php/detalle_sala.php?id_amb=13" target="_blank">Escenario al Aire Libre del mARTadero</a>,
el cual por cuenta propia ofrece grandes ventajas en cuanto a infraestructura y
costos. Esto nos permite registrar el sonido de cualquier concierto, con
completa independencia del sistema de refuerzo sonoro que se utilizare, es
decir, que la calidad de la grabación no dependerá sino de nosotros.</p>
<p>El diseño interior de <strong>la sala de grabación sonora está dividido en
compartimentos para la realización de un trabajo más óptimo, rápido y
entretenido</strong>. Nos permite a su vez grabar a agrupaciones musicales
grandes como ser orquestas sinfónicas, de cámara, bandas, grupos autóctonos,
murgas, etc. El espacio también <strong>nos posibilita realizar jam sessions,
grabaciones experimentales, y registrar en audio y video interpretaciones
musicales y performances</strong> realizados en un momento dado.</p>

