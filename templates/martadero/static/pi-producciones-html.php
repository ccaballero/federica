El <strong>Proyecto mARTadero</strong> es <strong>uno de los espacios culturales
más prominentes de Bolivia</strong>. Esto se debe sobre todo a la labor
voluntaria realizada por su personal.</p>
<p>En el caso de las coordinaciones artísticas, la figura es particular, pues
desde los inicios del mARTadero, por mandato de su fundadora N.A.D.A. (Nodo
Asociativo para el Desarrollo de las Artes), cada área debe procurarse algún
medio para dar <strong>sostenibilidad a su trabajo por medio de la gestión de
proyectos</strong>, o por la vía empresarial como es el presente caso.</p>
<p>En esta tarea, solo arquitectura y diseño han podido lograrlo por medio de
<a href="http://www.fundacionimagen.org" target="_blank">Fundación Imagen</a>
(FI), y ahora, las áreas de Música y Audiovisual con
<a href="http://pi.martadero.org" target="_blank">&pi; Producciones</a>.</p>

<p>Esta es la principal motivación es establecernos como un <strong>mecanismo de
trabajo empresarial</strong> pero con un <strong>enfoque cultural e
institucional</strong>.</p>

