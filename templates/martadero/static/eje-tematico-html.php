<h1>Eje temático conceptual del mARTadero</h1>

<h2>2012<br />REDUCTOS TECNO-LÓGICOS</h2>

<p class="quote">¿No es el arte el sistema operativo del alma?<br />
&ldquo;El principio básico de la naturaleza no sería la
&ldquo;adaptación&rdquo;, sino la &ldquo;creatividad&rdquo;,<br />
como elemento básico de la evolución de un sistema automantenible,<br />
autorenovable, autosuficiente y autotrascendente&rdquo;</p>
<p class="quote"><strong>Fritjof Capra</strong></p>
<br />

<p><strong>REDUCTOS TECNO-LÓGICOS</strong> plantea -como eje temático de
reflexión para las áreas, los programas y las diversas actividades del 2012- la
complejidad necesaria y poética que debe relacionar y establecer un diálogo de
<strong>espacios</strong> con <strong>ideas</strong> y con
<strong>recursos</strong> instrumentales o tecno-lógicos.</p>
<p><strong>Espacios</strong> que son hoy en día redes y ductos, entramados y
canales, tendiendo a ser cada vez más abiertos y conectivos pero también a
permitir el aislamiento creativo necesario, el claro en el bosque. Espacios, por
tanto, suficientemente <strong>protegidos</strong> mediante valores y
principios, pero en ningún caso aislados ni cerrados a la interacción. Espacios
que, como la etimología latina de <em>spatium</em> sugiere, se <em>realizan</em>
al <strong>estar</strong> conceptual y físicamente
<strong>abiertos</strong>.</p>
<p>Lo <strong>tecno-lógico</strong> entendido, a su vez, como la resignificación
de los medios desde una idea básica y central de <strong>pertinencia</strong>,
enfocándose sobre todo al <strong>gesto</strong> creativo como aporte
fundamental del artista. Un artista que, como creador, cree y confía en la
<strong>poética</strong> como transformadora de la condición humana, como
generadora de nuevas miradas que satisfagan necesidades internas no siempre
manifiestas, pero continuamente presentes.</p>
<p>Necesidades que encuentran precisamente en el <em>gesto</em> su sentido más
profundo, y en el <em><strong>oficio</strong></em> (en el <em>saber hacer</em> o
<em>tekhné</em> en griego y ars en latino), su verdadera satisfacción, su
capacidad evolutiva. Un <em><strong>saber hacer </strong></em>ubicado por tanto
<em>contextual</em> y <em>temporalmente</em>, que se expresa como tecnología de
cada momento y lugar, como manifestación del ingenio humano, de su ingente
capacidad de <strong>adaptación</strong> creativa al entorno, y que hoy se
despliega casi sin límites en un medio digital y de conectividades crecientes, y
que por su propia esencia <strong>comunicativa</strong> e
<strong>interactiva</strong>, tienen en <strong>lo colaborativo</strong> una
fuerte razón de ser, diluyendo autorías individuales y facilitando creaciones
colectivas y procesuales, que permiten a su vez <strong>democratizar</strong> la
cultura desde lo cotidiano y fomentar el <strong>movimiento
creativo</strong>.</p>
<p>Todo ello en un campo de <strong>realidades físicas y virtuales</strong> cada
vez más sugerentemente <strong>vinculadas</strong>, donde pervivencias
erróneamente denominadas <em>pretecnológicas</em> coexisten orgánicamente con
proyecciones futuristas mal pronosticadas, en medio de un continuo temporal del
que en realidad poco conocemos, y desde una condición humana que sigue siendo
siempre el único trasfondo válido y universal de nuestro trabajo creativo.</p>
<p>Espacio. Idea. Recursos. Establecer nuestro reducto tecno-lógico. Crear
intuitiva y lógicamente nuestro propio espacio, con nuestro espacio, desde
nuestro espacio.</p>
<p>Crear para <strong>crearnos a nosotros mismos</strong> en el mundo.</p>
