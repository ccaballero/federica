<h3>POLÍTICAS CULTURALES Y REDES</h3>
<p><img src="<?php echo $this->staticUrl('/politicas-culturales.jpg') ?>" title="Políticas Culturales mARTadero" /></p>
<p>Política cultural se entiende como "aquella área de legislación pública que
rige las actividades relacionadas a las artes y la cultura e involucra, por lo
general, la gestación de procesos, clasificaciones legales e instituciones que
promuevan la diversidad cultural y la accesibilidad, así como la promoción y
difusión de las expresiones artísticas, sociolinguísticas, y literarias de todos
los pueblos".</p>
<p>Es un programa mediante el que queremos impulsar el avance de Políticas
Culturales en nuestro país, ya que la posibilidad de abrir el debate a través de
plataformas de comunicación se presenta como una necesidad de las sociedades
democráticas contemporáneas. Es por ello que mediante el blog, símbolo de
reciprocidad y espacio abierto de intercambio de opiniones, esperamos
accesibilizar este debate, con la intención de constituirse en un aporte de
Proyecto mARTadero al proceso actual de gestación de una política cultural para
nuestro país. Intentamos así impulsar tres leyes concretas de alta prioridad:
una relativa a los Puntos de Cultura, otra relacionada a los Espacios Culturales
y una última ligada a los Fondos Concursables de Promoción Artística.</p>
<p><a href="<?php echo $this->url(array('page' => 'politicas-culturales.html'), 'base_static') ?>"><?php echo $this->url(array('page' => 'politicas-culturales.html'), 'base_static') ?></a></p>
<p><a href="http://politicasculturalesblog.wordpress.com/">http://politicasculturalesblog.wordpress.com/</a></p>

<h3>VIVERO DE EMPRENDIMIENTOS ARTíSTICO-CREATIVOS</h3>
<p><img src="<?php echo $this->staticUrl('/vivero.jpg') ?>" title="Vivero mARTadero" /></p>
<p>Uno de los principales problemas del sector artístico cultural es que no
cuenta con el conocimiento necesario para desarrollarse en el mundo empresarial
ni para generar organizaciones legalmente establecidas. A su vez, carece de una
estructura organizativa idónea para establecer relaciones de mercado a nivel
nacional e internacional. Es por ello que junto a Fundación Imagen, proponemos
un proyecto que apoye a los artistas emprendedores, brindándoles la posibilidad
de utilizar su creatividad para generar una estrategia integral que contribuya
al desarrollo económico y social en Bolivia. El objetivo principal es poder
llegar a proveer a aquellos interesados en profesiones artísticas un entorno con
infraestructuras adecuadas, asesoría en marketing, ventas y comercialización,
servicio de tutores o mentores empresariales y apoyo en el seguimiento de la
gestión, entre otros.</p>
<p>Con este programa buscamos:</p>

<ul>
    <li>Promover el desarrollo económico del sector artístico y creativo,
    contribuyendo a la organización del sector cultural y creativo, y a su
    reconocimiento como un ámbito que cada vez alcance mayor protagonismo en la
    dinámica económica mundial.</li>
    <li>Capacitar a los artistas para crear empresas artísticas y creativas, y
    fortalecer las pocas existentes, brindando un espacio con el equipamiento
    técnico y logístico para que consoliden su trabajo de manera profesional,
    fortaleciendo así la oferta cultural de calidad.</li>
    <li>Dotar de las herramientas necesarias en cuanto a conocimientos
    relacionados con las diferentes áreas de la administración, gestión de
    empresas y nuevas tecnologías en el ámbito cultural y artístico.</li>
</ul>

