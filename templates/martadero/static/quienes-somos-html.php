<p>El mARTadero es un proyecto integral de desarrollo social a través del arte
y la cultura.</p>

<p>Como proyecto integral de desarrollo artístico y cultural para el cambio
social, <strong>trabajamos por y para el desarrollo social a través del arte y
la cultura</strong> a partir de <strong><a href="#areas">7 áreas de creación
artística</a></strong> y <strong><a href="#programas">7 programas de desarrollo
social</a></strong>, todos basados en <strong><a href="#principios">7
principios</a></strong>. A su vez, basada en 3 pilares:</p>

<ul>
    <li><strong>El espacio:</strong> el conjunto arquitectónico ex-matadero: de
    3.000 m², construido en 1924, de carácter manifiestamente patrimonial,
    pendiente de declaratoria como exponente privilegiado y auténtico del anhelo
    de progreso propio de la arquitectura industrial de inicios del siglo XX,
    único, flexible, elocuente, descentralizado, estratégico social y
    geográficamente, adecuado a la lógica y las necesidades de las artes
    emergentes, y cedido por 30 años a NADA para el desarrollo autogestionario
    del proyecto.</li>
    <li><strong>La gestión cultural:</strong> un equipo profesional
    multidisciplinario y abierto, altamente cualificado en la promoción
    intercultural en ámbitos locales, nacionales e internacionales, con
    autonomía de gestión y carácter asambleario, bien estructurado
    organizativamente, con procedimientos enfocados a obtener resultados claros
    y pertinentes para la sociedad, y capaz de irse regenerando y proyectando
    según el signo de los tiempos y las necesidades prioritarias, convencido de
    las ventajas de la conexión, de las redes y de la urgencia de ponernos de
    acuerdo en una Cultura de Futuro.</li>
    <li><strong>Los principios:</strong> siete máximas que se basan en una
    intención clara de innovación, investigación, experimentación, rigor
    conceptual y formal, integración, intercambio e interculturalidad, como
    criterios aplicables a toda propuesta generada, impulsada, apoyada u
    organizada por el proyecto, y que manifiestan los valores intrínsecos a su
    actuar y el convencimiento de nuestra capacidad de generar un mejor futuro.
    </li>
</ul>

<h2><a name="areas"></a>7 ÁREAS DE CREACIÓN ARTÍSTICA</h2>
<p>En estos seis años de vida, como proyecto mARTadero, hemos realizado casi 800
actividades de carácter diverso, recibiendo en el lugar a más de 200.000
asistentes. Hoy por hoy es el proyecto cultural más innovador y de mayor
crecimiento de Bolivia, y un ejemplo en la región, gracias al trabajo de más de
treinta personas que a lo largo de este tiempo han colaborado con su esfuerzo y
capacidad de gestión. Parte importante de dicho logro son las siete áreas de
creación artística que plantean como objetivo el fomento y desarrollo de su
ámbito artístico en la ciudad de Cochabamba no solo a través de exposiciones,
sino, además, mediante la activación de dispositivos de formación y desarrollo
con una especial inclinación hacia el arte emergente. A continuación detallamos
los objetivos particulares de cada una de ellas:</p>
<p><img src="<?php echo $this->staticUrl('/estrella-areas.jpg') ?>" title="Areas de Creación Artística" /></p>

<h3>ARTES VISUALES Y FOTOGRAFíA</h3>
<p><img src="<?php echo $this->staticUrl('/artes-visuales.jpg') ?>" title="Artes Visuales mARTadero" /></p>
<p>A través de esta área buscamos posicionar la creación visual contemporánea en
el panorama boliviano e internacional, mediante exposiciones, eventos,
intercambios, participación en redes internacionales, talleres de formación,
fomento de artistas bolivianos en el extranjero, etc. Un eje esencial es el
posicionamiento del arte como espacio de construcción social, a través del
despliegue multidimensional de la creatividad en el contexto.</p>

<h3>LETRAS</h3>
<p><img src="<?php echo $this->staticUrl('/letras.jpg') ?>" title="Letras mARTadero" /></p>
<p>Con el área de Letras buscamos promover la creación, la difusión y el
intercambio de propuestas literarias, a través de talleres, producción y
participación en actividades ligadas a la escritura, tanto a nivel nacional como
internacional. A la vez de otorgar especial atención a un conocimiento basado en
la experiencia individual y colectiva, con el fin de ir generando nuevas voces
poéticas y narrativas cada vez más conscientes, críticas y propositivas ante sí
mismas y del entorno en el que habitan.</p>

<h3>ARTES ESCÉNICAS</h3>
<p><img src="<?php echo $this->staticUrl('/artes-escenicas.jpg') ?>" title="Artes Escénicas mARTadero" /></p>
<p>Mediante esta área buscamos impulsar la formación de creadores bolivianos a
través de la producción de actividades relacionadas con las distintas tendencias
y géneros dentro de las artes escénicas (Teatro, Danza, Títeres, etc.). Además,
velamos porque dichas expresiones valoren el contexto como fuente de creación y
construcción social. Éstas las evaluamos y llevamos a cabo en correspondencia
con los principios que rigen para todos los proyectos generados en mARTadero.
</p>

<h3>DISEÑO GRÁFICO Y ARQUITECTURA</h3>
<p><img src="<?php echo $this->staticUrl('/arquitectura.jpg') ?>" title="Arquitectura mARTadero" /></p>
<p>Con esta área buscamos contribuir al desarrollo gráfico y arquitectónico de
Cochabamba y Bolivia, a través del trabajo interdisciplinario en diversas
escalas por medio de cursos, proyectos, encuentros, redes e intervenciones
significativas. Asimismo, guiamos la recuperación progresiva del sitio ex
matadero, dinamizando su carácter patrimonial, adecuando los ambientes para el
uso cultural y artístico como patrimonio de la ciudad, y de otras construcciones
históricas desde una necesaria dimensión ambiental y bioclimática de su
arquitectura.</p>

<h3>AUDIOVISUAL</h3>
<p><img src="<?php echo $this->staticUrl('/audiovisual.jpg') ?>" title="Audiovisual mARTadero" /></p>
<p>Por medio de esta área buscamos impulsar la producción audiovisual
experimental y profesional en nuestro medio, promoviendo y produciendo
actividades creativas, formativas y expositivas de diversos géneros y
tendencias. Apostamos por el trabajo interdisciplinario artístico, por la
exploración del lenguaje audiovisual y por la composición de lógicas de
producción alternativas adecuadas para nuestro contexto ecónomico-cultural.</p>

<h3>MÚSICA</h3>
<p><img src="<?php echo $this->staticUrl('/musica.jpg') ?>" title="Musica mARTadero" /></p>
<p>Con Música buscamos facilitar, promover y difundir la producción de eventos
musicales (conciertos, talleres, etc.), sin discriminar ningún género musical y
con un enfoque interdisciplinar. Priorizamos y concentramos nuestro trabajo en
la creación musical realizada por artistas nuevos, especialmente aquellos que
experimentan en el camino de la composición y que por razones diversas no pueden
acceder a espacios para el fortalecimiento y la expresión de su arte y talento.
</p>

<h3>INTERACCIÓN SOCIAL</h3>
<p><img src="<?php echo $this->staticUrl('/interaccion-social.jpg') ?>" title="Interaccion Social mARTadero" /></p>
<p>A través de esta área buscamos impulsar procesos de desarrollo social a
través de estrategias artístico-culturales, generando un movimiento progresivo
de futuro basado en la creatividad y en el rol protagonista de la cultura que
posibilite inclusión, conciencia ambiental, producción económica y cohesión
social. Asimismo, buscamos la interacción de los artistas con el público en
general, poniendo principal atención en las nuevas generaciones, verdaderos
constructores de una cultura de futuro.</p>

<h2><a name="programas"></a>7 PROGRAMAS DE DESARROLLO SOCIAL</h2>
