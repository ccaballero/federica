<h3>RESIDENCIAS ARTÍSTICAS PRANA</h3>
<p><img src="<?php echo $this->staticUrl('/prana.jpg') ?>" title="Prana mARTadero" /></p>
<p><strong>PRANA</strong> es una palabra en sánscrito se refiere a "lo vital", a
la <strong>fuerza de las cosas vivas y la energía generadora en el proceso
natural del universo</strong>. Viene de los Upanishads, y sugiere esa parte de
la realidad física que es el soporte del cuerpo y la madre de la mente y el
pensamiento.</p>
<p>PRANA (Programa de Residencias Artísticas de NADA) es un programa que
consiste en habitaciones construidas dentro de nuestro espacio, impulsadas por
Fundación Imagen y con la ayuda de la Embajada de Holanda, orientadas a recibir
artistas y gestores culturales que en el plazo de una semana a cuatro (para
artistas nacionales) o de cuatro a ocho semanas (para artistas internacionales,
produzcan una obra. La propuesta (obra física o proceso) habrá de contribuir al
barrio de Villa Coronilla o a la ciudad de Cochabamba, buscando la interacción
social de la ciudadanía con el trabajo del artista.</p>
<p>Se trata, pues, de idear proyectos artísticos pertinentes para el lugar,
realizables en el tiempo de residencia, y trabajando e incorporando el contexto
(dejándose interpelar por él).</p>
<p>La consecución de los gastos de material y equipos necesarios para la
producción de la obra correrán a cargo del centro receptor, a través de la
gestión de fundaciones.</p>

<ul>
    <li>La capacidad máxima está establecida en 8 plazas (4 en cada cuarto) en
    PRANA-Norte, y 6 plazas en PRANA-Sud, haciendo un total de 14.</li>
    <li>Los artistas y gestores que vengan con programas de residencias tendrán
    prioridad sobre cualquier otro hospedado.</li>
    <li>Para artistas interesados en residir de forma independiente, fuera de
    los programas de residencias, existe igualmente la posibilidad.</li>
</ul>

