<p>El mARTadero es un proyecto integral de desarrollo social a través del arte
y la cultura.</p>

<p>Como proyecto integral de desarrollo artístico y cultural para el cambio
social, <strong>trabajamos por y para el desarrollo social a través del arte y
la cultura</strong> a partir de <strong><a href="<?php echo $this->url(array(), 'areas_list') ?>">7 áreas de creación
artística</a></strong> y <strong><a href="<?php echo $this->url(array(), 'programs_list') ?>">7 programas de desarrollo
social</a></strong>, todos basados en <strong><a href="#principios">7
principios</a></strong>. A su vez, basada en 3 pilares:</p>

<ul>
    <li><strong>El espacio:</strong> el conjunto arquitectónico ex-matadero: de
    3.000 m², construido en 1924, de carácter manifiestamente patrimonial,
    pendiente de declaratoria como exponente privilegiado y auténtico del anhelo
    de progreso propio de la arquitectura industrial de inicios del siglo XX,
    único, flexible, elocuente, descentralizado, estratégico social y
    geográficamente, adecuado a la lógica y las necesidades de las artes
    emergentes, y cedido por 30 años a NADA para el desarrollo autogestionario
    del proyecto.</li>
    <li><strong>La gestión cultural:</strong> un equipo profesional
    multidisciplinario y abierto, altamente cualificado en la promoción
    intercultural en ámbitos locales, nacionales e internacionales, con
    autonomía de gestión y carácter asambleario, bien estructurado
    organizativamente, con procedimientos enfocados a obtener resultados claros
    y pertinentes para la sociedad, y capaz de irse regenerando y proyectando
    según el signo de los tiempos y las necesidades prioritarias, convencido de
    las ventajas de la conexión, de las redes y de la urgencia de ponernos de
    acuerdo en una Cultura de Futuro.</li>
    <li><strong>Los principios:</strong> siete máximas que se basan en una
    intención clara de innovación, investigación, experimentación, rigor
    conceptual y formal, integración, intercambio e interculturalidad, como
    criterios aplicables a toda propuesta generada, impulsada, apoyada u
    organizada por el proyecto, y que manifiestan los valores intrínsecos a su
    actuar y el convencimiento de nuestra capacidad de generar un mejor futuro.
    </li>
</ul>

<h2><a name="principios"></a>7 PRINCIPIOS</h2>
<p>Los criterios que aplicamos a toda propuesta, se fundamentan en los mismos
principios por los que se rige la asociación NADA en sus estatutos,
constituyendo uno de los 3 pilares básicos del proyecto. Estos son:</p>

<h3>INNOVACIÓN</h3>
<p><img src="<?php echo $this->staticUrl('/innovacion.jpg') ?>" title="Innovación mARTadero" /></p>
<p>Una propuesta es innovadora cuando sus contenidos conceptuales van mas allá
de lo dado o establecido; cuando su puesta en escena o aspecto formal es
coherente con la originalidad del contenido.</p>

<h3>INVESTIGACIÓN</h3>
<p><img src="<?php echo $this->staticUrl('/investigacion.jpg') ?>" title="Investigación mARTadero" /></p>
<p>La propuesta debe ser producto de investigaciones y reflexiones acerca del
contenido propuesto y sus implicaciones socioculturales.</p>

<h3>EXPERIMENTACIÓN</h3>
<p><img src="<?php echo $this->staticUrl('/experimentacion.jpg') ?>" title="Experimentación mARTadero" /></p>
<p>Una propuesta es experimental cuando sus contenidos y por tanto su puesta en
escena cuestionan responsablemente los límites de lo artístico o estéticamente
establecido.</p>

<h3>RIGOR CONCEPTUAL Y FORMAL</h3>
<p><img src="<?php echo $this->staticUrl('/rigor.jpg') ?>" title="Rigor Conceptual y Formal mARTadero" /></p>
<p>El rigor conceptual y formal de una propuesta se refiere tanto a la
coherencia lógica discursiva de sus propuestas conceptuales, como a la adecuada
factura de la puesta en escena.</p>

<h3>INTEGRACIÓN</h3>
<p><img src="<?php echo $this->staticUrl('/integracion.jpg') ?>" title="Integración mARTadero" /></p>
<p>La integración es un concepto amplio que involucra la capacidad de integrar
disciplinas o lenguajes artísticos, pero también la necesidad de integración
sociocultural desde una perspectiva plural.</p>

<h3>INTERCAMBIO DE CONOCIMIENTOS Y EXPERIENCIAS.</h3>
<p><img src="<?php echo $this->staticUrl('/intercambio.jpg') ?>" title="Intercambio mARTadero" /></p>
<p>Se refiere a la necesidad de que una propuesta tenga o proponga consecuencias
cognoscitivas en la sociedad, de tal manera que pueda generar conocimiento más
que acumulación de información. Se refiere a la formación crítica que supone un
conocimiento integrador, comunal, dialógico y dialéctico.</p>

<h3>INTERCULTURALIDAD</h3>
<p><img src="<?php echo $this->staticUrl('/interculturalidad.jpg') ?>" title="Interculturalidad mARTadero" /></p>
<p>La interculturalidad supone no solo respeto a la diversidad cultural, sino
también el fomento, conocimiento y la valoración de sus manifestaciones
artísticas y culturales.</p>
<p>Estamos convencidos de que un arte y una cultura vivos y participativos
contribuyen fundamentalmente al desarrollo y bienestar social, y, por ello,
consideramos prioritario el generar un espacio amplio e inclusivo (físico y
conceptualmente) para la creación.</p>

<h2>Nuestro espacio ha de servir:</h2>
<ul>
    <li>Para la creación sinergética y la innovación artística/cultural
    multidisciplinaria.</li>
    <li>Para la visibilidad y accesibilidad de la oferta cultural
    emergente.</li>
    <li>Para la investigación y aplicación de nuevos modelos de gestión
    cultural.</li>
    <li>Para la interacción e integración social a través del arte.</li>
    <li>Para ir más allá de límites y fronteras nacionales, hacia una verdadera
    interculturalidad desde las identidades dinámicas e incluyentes.</li>
    <li>Para la identificación de liderazgos enfocados a la detonación de
    procesos.</li>
    <li>Para la generación de flujos y la inserción en redes.</li>
</ul>
