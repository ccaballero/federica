<h3>TEXTO &lt;ESP&gt;</h3>
<p><a href="http://www.residenciasenred.org">Residencias_en_red [iberoamérica]</a>
es una plataforma iberoamericana de espacios de investigación, producción y
exhibición de arte y cultura contemporánea, que están vinculados principalmente
a través de sus programas de residencias. La red reúne diversos formatos de
residencias y pone en valor esa diversidad. Está formada por proyectos privados
y de iniciativas mixtas público-privadas con estructuras administrativas y
legales diversas. La red se crea en 2008 a través de un deseo de integración
latinoamericana y de un deseo afectivo de relación y trabajo conjunto entre sus
miembros para, a través de ella, conseguir representatividad e interlocución a
nivel micro y macro de la política cultural internacional.</p>

<h3>TEXTO &lt;PORT&gt;</h3>
<p><a href="http://www.residenciasenred.org">Residencias_en_red [iberoamérica]</a>
é uma plataforma ibero americana de espaços de pesquisa, produção e exposição de
arte e cultura contemporânea que estão vinculados, principalmente através de
seus programas de residência. A rede reúne diversos formatos de residências e
valoriza a esta diversidade. Formada por projetos privados e de iniciativas
mistas publica-privadas com estruturas administrativas e jurídicas diversas. A
rede se criou em 2008 através de um desejo de integração latino-americana e de
um desejo afetivo de relação e trabalho conjunto entre seus membros para através
dela conseguir representação e interlocução a nível micro e macro da política
cultural internacional.</p>

<h3>TEXTO &lt;ENG&gt;</h3>
<p><a href="http://www.residenciasenred.org">Residencias_en_red [iberoamérica]</a>
is an Ibero American platform of spaces working with research, production and
exhibition of contemporary art and culture, connected particularly through its
residency programs. It brings together different formats of art residencies and
enhances its diversity. It is formed by private or public-private initiatives
with various administrative and legal structures. The network was created in
2008 through an aspiration for Latin American integration and a desire for
collaboration and collective work among its members so to strengthen
representation and communication at the micro and macro level of international
cultural policy.</p>

