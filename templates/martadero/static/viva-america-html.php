<p><strong>CIRCUITO VIVA AMERICA</strong> es una nueva red de centros culturales
en América Latina asociada al Festival. Por primera vez el Festival VIVA AMERICA
llega a <a href="http://www.circuitovivaamerica.net/?page_id=8" target="_blank">Argentina</a>,
<a href="http://www.circuitovivaamerica.net/?page_id=22" target="_blank">Bolivia</a>,
<a href="http://www.circuitovivaamerica.net/?page_id=27" target="_blank">Chile</a>,
<a href="http://www.circuitovivaamerica.net/?page_id=33" target="_blank">México</a>
y <a href="http://www.circuitovivaamerica.net/?page_id=37" target="_blank">Venezuela</a>,
por medio de una programación de actividades culturales descentralizada.</p>

<p>Teatro, cine, proyectos de recuperación del patrimonio artístico material e
inmaterial, fotografía, conferencias, video documental conforman esta
programación paralela a la que VIVA AMÉRICA te invita en esta nueva edición.
Infórmate de la agenda de actividades si estás en las nuevas ciudades que nos
acompañan o sigue las actividades y proyectos a través desde esta web.</p>

