model for it</strong>.</p>
<p><strong>It is called PRANA (Artistic Residency Program of NADA)</strong>.
PRANA is also a Sanskrit word that refers to "what is vital", to the strength of
living things and to the generation of energy through the natural process of the
universe. It comes from the Upanishads, and it talks about that part of the
physical reality that supports the body and is the mother of mind and
thought.</p>

