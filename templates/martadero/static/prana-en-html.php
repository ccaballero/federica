<h3>RESIDENCY PROGRAM FEATURES</h3>
<p><strong>Start Year</strong>: 2009.<br />
<strong>Announcement system</strong>: mixed (invitation/contest).<br />
<strong>Number of places per year</strong>: 12.<br />
<strong>Minimum / maximum stay</strong>: 1 week / 1 month.</p>

<h3>PURPOSE OF THE RESIDENCY PROGRAM</h3>
<p>Cultural Exchange; improvement of the area and its context, Ibero-American
integration, social change.</p>

<h3>FEASIBILITY OF THE RESIDENCY PROGRAM</h3>
