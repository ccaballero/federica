<p>The artistic training of candidates will be essential, and their experience
in interdisciplinary and social processes will be assessed. The selection will
take into account:</p>

<ul>
    <li><strong>The project to develop</strong>: relevance, conceptual and
    formal stringency, ability to meet the deadline, interdisciplinary approach,
    and so on.</li>
    <li><strong>Experience and interest of the artist</strong>: his or her
    resume, proposals in previous works, motivation letter, and so on.</li>
    <li>Belonging to <strong>priority networks</strong>.</li>
</ul>

<h2>SPACE FEATURES</h2>
<ul>
    <li><strong>There is a top capacity of</strong> 8 people (4 in each room) in
