<h2>CONDITIONS</h2>
<p>The residences, lasting from one to four weeks (in the case of national
artists) and four to six weeks (for international artists), have the purpose of
producing a work. In the case of production projects (cultural management), the
stay can be extended to eight weeks.</p>
<p><strong>The logic in both cases is to leave the physical work, or do
something (a process) in the neighborhood of Villa Coronilla or the city of
Cochabamba</strong>, seeking social interaction of citizens with the artist's
work. The work's principles should be aligned comply with those of NADA,
mentioned above.</p>
<p>The idea is to <strong>devise appropriate art projects for the site,
achievable in the time of residence and that incorporate the context, (allowing
interaction with it)</strong>.</p>
<p>The receiving center, through foundations, will take care of raising the
funds for the material and the equipment necessary for producing the work.</p>

<h2>CANDIDATES</h2>
<p><strong>All artists of any discipline and nationality</strong> with a desire
to suggest a clear and creative proposal for the improvement of the district and
the city along NADA's working lines, <strong>will be eligible for the residency
program PRANA</strong>. Priority will be given to artists from networks and
movements in which the mARTadero project is immersed, to strengthen them.</p>

