<h1>Participa en el mARTadero</h1>

<p>La programación de actividades culturales del mARTadero 2.0, se
<strong>reconfigura</strong> a partir de la experiencia y el camino recorrido
para afrontar pertinentemente los desafíos que ahora encaramos, relativos a
<strong>conexiones y redes</strong> nacionales e internacionales,
<strong>intermediación</strong> entre artistas y público, <strong>estrategias de
desarrollo barrial</strong> y contextual, <strong>promoción de la
creatividad</strong> en el fondo y en la forma, etc. Así, en esta nueva forma de
programación <strong>el énfasis está puesto en los procesos</strong>, en la
capacidad proyectiva y conectiva de las actividades, y en su potencial
detonador.</p>

<p>Es por ello que la lógica de programación se plantea en <strong>cuatro
modalidades complementarias</strong>, todas ellas articuladas por el
<strong>eje temático anual</strong>.</p>

<p>En 2012 el eje temático es
<a href="<?php echo $this->url(array('page' => 'eje-tematico.html'), 'base_static') ?>">Reductos
tecno-lógicos</a>.</p>

<h2>MODOS DE PROGRAMACIÓN</h2>
<p>Las modalidades de programación son formas complementarias de participación
artística que el proyecto mARTadero propone para que las actividades anuales se
vayan articulando y realizando alrededor del eje temático. Son las
siguientes:</p>

<h3>Invitando</h3>
<p>Consiste en una <strong>invitación directa a artistas y colectivos</strong>
cuya obra o recorrido resulta especialmente interesante, para que realicen
actividades dentro del mARTadero. Se invita al artista a <strong>aliarse con el
proyecto</strong> para realizar una <strong>actividad conjunta</strong> que
propongan oportunidades y beneficios mutuos.</p>

<p>Para + info sobre esta modalidad:</p>

<ul>
    <li><a href="<?php echo $this->url(array('page' => 'eje-tematico.html'), 'base_static') ?>">Eje
    temático</a>.</li>
    <li><a href="<?php echo $this->url(array('page' => 'contactos.html'), 'base_static') ?>#equipo">Equipo
    humano</a>: por si deseas realizar un contacto con algún área.</li>
</ul>

<h3>Intermediando</h3>
<p>El proyecto mARTadero actúa como un <strong>facilitador o mediador
cultural</strong> entre las propuestas de los creadores y su buena llegada al
público. La solicitud parte del artista, colectivo o institución, y la
coordinación de área la evalúa, la apoya para su realización en el espacio y
finalmente contribuye a llevarla a cabo. La actividad <strong>se realiza con los
recursos propios o internalizados de cada artista colectivo, o
institución</strong>, sujetos a las tablas y políticas de corresponsabilidad del
mARTadero.</p>

