<h3>Incentivando</h3>
<p>Consiste en el lanzamiento de una <strong>convocatoria general y otras
puntuales</strong> (conectadas a festivales, encuentros o premios) para que
artistas y colectivos orienten su trabajo al eje temático propuesto por el
mARTadero. Se realiza la primera de manera semestral, especificando condiciones
y otras puntuales (conectadas a ayudas para las propuestas a ser seleccionadas.
Se plantearán así beneficios especiales para los artistas seleccionados tales
como los bonos de espacios, equipos y residencia, y acceso a fondART si se
cobrase entrada.</p>

