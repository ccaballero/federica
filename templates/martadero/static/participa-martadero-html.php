<h3>Inventando</h3>
<p>Los coordinadores de áreas del proyecto mARTadero promueven actividades
creativas y significativas, de libre configuración basadas en <strong>el eje
temático propuesto</strong>. Se buscará en estos casos una preeminencia mayor de
los principios y líneas de trabajo del proyecto, siendo innovadora en fondo y
forma.</p>

