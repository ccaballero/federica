<h1>REDES CULTURALES<br />
Territorio Cultural - Centros del Sur</h1>

<p>El Territorio Cultural - Centros del Sur es la proyección de la estrella del
Sur en la Tierra, formando un espacio transfronterizo de espacios culturales y
artísticos. Las Conexiones Territoriales (C.T.) se establecen como nodos
neurálgicos de las interconexiones culturales y artísticas en este nuevo
territorio fronterizo.</p>
<p>Cuando elegimos el nombre de NADA para nuestra asociación, ya visualizábamos
el ser un Nodo de una red creciente. Porque <strong>la conexión</strong> -como a
su vez y en sentido opuesto la división- <strong>no tiene límites</strong>,
siempre puede ir más allá&hellip; Y en ese sentido hemos trabajado y seguiremos
haciéndolo, porque realmente creemos en ello.</p>
<p>El proyecto mARTadero ha de encarar así de forma más precisa el manejo de las
<strong>nuevas territorialidades</strong>, los ámbitos de trabajo e intercambio
(como el Territorio Cultural Centros del Sur) y el impulso de procesos y las
lógicas de conexión local, nacional, latinoamericana e internacional.</p>
<p>Y habremos de hacerlo, en los próximos años, mediante redes. Porque ellas,
como mecanismo de asociación informal pero eficaz, tienen un
<strong>potencial</strong> considerable para articular información, medios
económicos y humanos, fuerzas, contactos, etc. Su <strong>sinergia</strong>
dependerá de la comunicación y colaboración entre miembros, y por tanto del
fluido circulante en el sistema. Implican además una articulación entre
distintos actores de forma permanente o regular, que vaya creciendo en el tiempo
permitiendo la concreción progresiva de las propuestas y objetivos
conjuntos.</p>
<p>La participación en la misma tiene que ser <strong>dinámica</strong> y, por
tanto, <strong>dinamizada</strong>, e implica un necesario protagonismo de todos
los nodos de dicha red. Igualmente implica el sabio manejo de la tensión entre
la <strong>identidad individual</strong> de cada grupo y la
<strong>colectiva</strong> de la red en su conjunto, y la permanente tensión
para sostener la vitalidad de los grupos de trabajo como <strong>espacios
colectivos de decisión</strong>.</p>
<p><strong>El beneficio es obvio</strong>. La posibilidad de <strong>optimizar
recursos, potenciar intercambios, concretar iniciativas, resolver problemas,
generar espacios de participación, ampliar miradas y perspectivas, poner en
juego nuevas capacidades, construir desde la corresponsabilidad y el
compromiso</strong>, etc. serían ventajas más que suficientes para plantearse de
forma responsable un trabajo en red.</p>
<p><strong>La organización y la vigilancia permanente</strong> son esenciales
para un buen funcionamiento. Alguna persona que pueda dedicarse específicamente
a ello también es importante. Transformando la famosa frase de Picasso: <em>"la
sinergia existe, pero te tiene que encontrar comunicando"</em></p>
<p>Las redes, como lógica, generarán toda una <strong>reconfiguración
territorial y organizativa</strong> en el sector cultural y en las relaciones
internacionales que le atañen. Por ello, el proyecto mARTadero se fija como
objetivo a corto y medio plazo la participación activa y la dinamización de las
redes a las que pertenece.</p>
<p>Aqui puedes ver un ejemplo de cómo puede funcionar <em>neurálgicamente</em>
los contactos entre distintos espacios culturales de manera transfronteriza a
través del <strong>Territorio Cultural - Centros del Sur</strong>, red impulsada
desde el mARTadero y que en 2009 empezó a dar sus primeros frutos.</p>

