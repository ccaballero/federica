<h1>Bienal de Arte Urbano | BAU</h1>

<p><a href="http://bau.martadero.org" target="_blank">
<img src="http://bau.martadero.org/wp-content/uploads/2011/08/Marca_BAU.jpg" width="287" height="335" /></a>
La Bienal de Arte Urbano de Cochabamba (<strong>BAU</strong>) ha sido concebida
como <strong>fruto de una progresiva y colaborativa labor de resignificación de
la ciudad y sus límites</strong> a través de distintas expresiones
artísticas.</p>
<p>Este evento parte de un <strong>largo proceso de reconfiguración del espacio
público</strong>. Está concebido como la mejor celebración del trabajo realizado
por el proyecto mARTadero a favor de la mejora del barrio de Villa
Coronilla.</p>
<p>La <strong>misión</strong> de la Bienal de Arte Urbano es la de
<strong>promover, mediante la acción y el arte, una recualificación y un
desarrollo de la zona mencionada</strong>, cuyo punto de partida nos remite al
centro histórico de Cochabamba. Esto debido a que en la actualidad "Villa
Coronilla" experimenta una situación de abandono y descuido en los aspectos de
ordenamiento urbano, cuidado patrimonial y otra serie de factores que han
incidido en la generalización de un imaginario social negativo que recae a
diario sobre sus habitantes.</p>
<p>Bajo este panorama, <strong>la BAU forma parte de una estrategia colaborativa
de mejoramiento urbano</strong> junto a la
<a href="http://www.martadero.org/propuestas-culturales/plazadelasartes.html">Plaza</a>
y el <a href="http://www.martadero.org/propuestas-culturales/paseo-de-las-artes.html">Paseo
de las Artes</a>. Su aporte principal será la puesta en valor de espacios,
objetos, muros y calles con una propuesta artística pensada siempre en pro del
desarrollo social y bajo los siete principios del mARTadero (innovación,
investigación, experimentación, rigor conceptual y formal, integración,
intercambio e interculturalidad) como una punta de lanza que dé inicio a la
transformación positiva no sólo de la estructura sino también del imaginario
social antes planteado.</p>

<blockquote>+ info: <a href="http://bau.martadero.org" target="_blank">bau.martadero.org</a></blockquote>

<h2>Instituciones involucradas</h2>
<p>Dado que la BAU es un proyecto de incidencia social, enmarcado en el
<a href="http://www.martadero.org/propuestas-culturales/paseo-de-las-artes.html">Paseo
de las Artes</a>, <strong>proyecto impulsado desde el mARTadero</strong>, que
busca la revalorización de la zona de "Villa Coronilla", las instituciones y
personas aliadas a este evento, son aquellas adscritas a la <strong>Plataforma
Interinstitucional por el Bien Común</strong>: Alberto Rivero, Parroquia Pío X,
Colegio de Arquitectos de Cochabamba, Escuela de música Man Césped, Escuela de
Artes Raúl G. Prada, Cooperativa Pío X. OTB's: Gerónimo de Osorio, Villa
Coronilla y Bartolomé Guzmán. Gobierno Autónomo Municipal de Cochabamba:
Dirección de Planificación, Dirección de Turismo, Oficialía de Servicios
Técnicos, Oficialía Mayor de Cultura, Secretaría de protección de la madre
tierra, Departamento de Patrimonio Territorial y la Oficialía de Planificación
Urbana.</p>
<p>Bajo este panorama, <strong>la BAU -evento organizado por Fundación Imagen
[FI] que cuenta con el patrocinio de Hivos y es gestionada desde el proyecto
mARTadero</strong>- es la continuación de una profunda etapa de transformación y
desarrollo de la zona a nivel: cultural, artístico, social, patrimonial,
económico, etc. que se apoya en un proceso formativo, el
<a href="http://bau.martadero.org/?page_id=130" target="_blank">Conart
Process</a>.</p>

<h2>BAU 2011</h2>
<p>La <strong>Primera Bienal de Arte Urbano se desarrolló del 23 de septiembre
al 8 de octubre de 2011</strong>. La exposición contó con <strong>más de 30
artistas y colectivos nacionales e internacionales</strong> que dejaron una
arca significativa con sus intervenciones en el tejido urbano. El 85% de las
obras se concentraron en las zona de las OTB's Villa Coronilla, Gerónimo de
Osorio y Bartolomé Guzmán, formando parte del recorrido del Paseo de las Artes.
Poco a poco este sector se configura como epicentro artístico de la ciudad,
teniendo en cuenta que en el mismo es posible encontrar la academia de música
Man Cespedes, desde hace seis años, el proyecto mARTadero y está prevista la
pronta construcción de un espacio para la escuela de bellas artes Raúl G.
Prada.</p>
<p>Todo ello inició con el Conart Process 2011-2012 consistente en la
realización de clínicas y talleres de arte urbano, que durante tres meses fueron
impartidos por los colectivos
<a href="http://www.basurama.org" title="Basurama" target="_blank">Basurama</a>
(España),
<a href="http://www.6emeia.com/" title="6EMEIA" target="_blank">6EMEIA</a>
(Brasil), Verónica Weiss (Suiza) y Paulina Varas (Chile) de los cuales fueron
partícipes 15 artistas nacionales, invitados a formar parte de la Bienal.</p>

