<h1>Memoteca</h1>

<p><img src="<?php echo $this->staticUrl('/fuera2.jpg') ?>" class="box_image" />
Sabemos que <strong>llevamos adelante una experiencia única</strong>,
trabajando desde lo local con visión global, y consolidando un espacio
patrimonial ejemplar.</p>
<p>Espacio (de spatium, conectado con patere) por "estar abierto", física y
conceptualmente. Y patrimonial porque patrimonio es, según la Carta de Cracovia
(2000), el <strong>"conjunto de obras del hombre en las cuales una comunidad
reconoce sus valores específicos y articulares y con los cuales se
identifica"</strong>.</p>
<p>Y estamos lográndolo mediante un cambio de mirada hacia las posibilidades del
patrimonio, hacia la necesidad de equipos multidisciplinares, hacia la
pertinencia de una gestión cultural moderna y eficiente&hellip;</p>
<p>Decidimos por ello la creación de una sala en el mARTadero que denominaremos
<strong>memoteca</strong>, o <strong>sala "de la memoria derruida"</strong>,
donde se mostrarán imágenes, filmaciones, planos y fragmentos de la ciudad del
ayer, rescatados de las casas patrimoniales del casco histórico y del entorno
del matadero demolidas en los últimos años&hellip; Porque la identificación, la
especificación, y la conformación de patrimonio es un proceso relacionado con la
elección de valores, como bien dice la misma Carta.</p>
<p>Y por ello, a los valores esenciales en el devenir de nuestro proyecto -que
fueron enunciados como principios de innovación, investigación, experimentación,
rigor conceptual y formal, integración, intercambio e interculturalidad- se les
han ido uniendo aquellos provenientes de la lectura cotidiana de la realidad,
para enriquecerlos&hellip;</p>
