<p><img src="<?php echo $this->staticUrl('/prAna.jpg') ?>" class="box_image" />
PrAna es un <strong>sistema de residencias</strong> que es parte del mARTadero,
un proyecto integral de desarrollo artístico cultural ubicado en, Villa
Coronilla, zona fundacional de la ciudad de Cochabamba. El mARTadero cuenta con
<strong>espacios equipados y con constantes eventos multidisciplinarios</strong>
que posibilita estar rodeado de la <strong>creación local</strong>.</p>

<p>En ese sentido, <strong>la Residencia artística prAna es más que un
refugio</strong> con ciertas facilidades para artistas y gestores culturales; es
parte de <strong>una dinámica colectiva</strong> que se nutre <strong>de
experiencias y aproximaciones</strong> que propician <strong>convivencias
creativas</strong> fundamentales en una <strong>búsqueda
artística-vital</strong>.</p>

