<p>Hoy por hoy, el proyecto mARTadero, es <strong>el proyecto cultural más
innovador y de mayor crecimiento de Bolivia</strong>.</p>

<p>Para desarrollar aún más los principios citados y conectar a Bolivia con el
panorama internacional, el proyecto mARTadero -ayudado por la
<a href="http://www.fundacionimagen.org" target="_blank">Fundación Imagen</a> y
la  Embajada de Holanda- ha construido en el sitio y desarrollado un modelo de
gestión para unas residencias orientadas a artistas y gestores culturales,
llamadas <strong>PRANA (Programa de Residencias Artísticas de
NADA)</strong>.</p>

<p><strong>PRANA</strong> es además una palabra en sánscrito se refiere a "lo
vital", a la <strong>fuerza de las cosas vivas y la energía generadora en el
proceso natural del universo</strong>. Viene de los Upanishads, y sugiere esa
parte de la realidad física que es el soporte del cuerpo y la madre de la mente
y el pensamiento.</p>

<h2><a name="modalidades"></a>MODALIDADES</h2>
<p><a href="<?php echo $this->staticUrl('/zoomwooz.jpg') ?>" class="highslide box_image" onclick="return hs.expand(this);">
<img src="<?php echo $this->staticUrl('/zoomwooz-small.jpg') ?>" /></a>
El <strong>Programa de Residencias Artísticas</strong> prAna tiene <strong>4
modalidades de funcionamiento</strong>: las Residencias Artísticas prAna, prAna
Pascana, prAna Catapulta y prAna Redes.</p>

<h3>Residencias Artísticas prAna</h3>
<p>Esta modalidad esta abierta a todos aquellos artistas, gestores culturales e
investigadores de cualquier disciplina y nacionalidad con deseo de plantear una
propuesta clara y creativa, un trabajo realizable en el tiempo de residencia,
ya sea asumido como trabajo en proceso o con objetivos muy concretos. Pero sobre
todo proyectos conscientes de la experiencia de vivir y trabajar en Villa
Coronilla y el mARTadero, es decir que se incorporen al contexto y que se dejen
interpelar por él.</p>

<p><a href="<?php echo $this->staticUrl('/mayra-montoya.jpg') ?>" class="highslide box_image" onclick="return hs.expand(this);">
<img src="<?php echo $this->staticUrl('/mayra-montoya-small.jpg') ?>" /></a>
Las residencias podrán tener una duración a partir de 2 a 8 semanas, las mismas
pueden ser extendidas, de acuerdo a las necesidades del proyecto presentado y
en relación a los requerimientos de producción de la obra.</p>

<p><img src="<?php echo $this->templateUrl('/images/flecha_blog.jpg') ?>" alt="" title="" />
Existen <strong>3 maneras de participar</strong> en el programa de
residencias:</p>

<h3>Autogestionado por el residente</h3>
<p>El Residente llega por sus propios medios y paga un costo bastante razonable
por la Residencia y todos sus beneficios.</p>
<p>Para informarte sobre el <a href="#aporte">aporte por uso de estadía en las
residencias pincha aquí</a></p>
<p>Estos aportes por uso están realizados según una escala de países por el IDH
(Índice de Desarrollo Humano) que
<a href="https://docs.google.com/spreadsheet/ccc?key=0AtuhBpIn0Kx8dFN3WHNQbEh4UHgzbHlEQ0ZydGYyRVE" target="_blank">puedes consultar aquí</a>.</p>

<p>Para participar en la Residencia en la modalidad de autogestionada envíanos
con anticipación el siguiente formulario en cualquier momento del año a:</p>

<blockquote>
<a href="https://docs.google.com/a/martadero.org/spreadsheet/viewform?formkey=dFZ5Q2dCRUVsaVlxNmNGTEZFbFZJQ2c6MQ#gid=0" target="_blank">Formulario de REGISTRO DE RESIDENTES prAna AUTOGESTIONADA</a>
</blockquote>

<p>La <strong>selección tendrá en cuenta</strong>:</p>
<ul>
    <li>La <strong>experiencia e interés del artista</strong>: su hoja de vida,
    líneas de investigación, obras anteriores, etc. La experiencia artística de
    los candidatos, será valorada así como su desempeño en procesos
    interdisciplinarios y sociales.</li>
    <li>El <strong>proyecto a desarrollar</strong>: pertinencia, rigor
    conceptual y formal, posibilidad de realizarlo en el tiempo solicitado,
    enfoque interdisciplinar, etc.</li>
</ul>

<h3>Programas</h3>
<p>Convenios o apoyos provenientes de instituciones vinculadas a Arte,
Universidades, etc.</p>

<h3>Redes</h3>
<p>Intercambios a través de Redes. Generalmente esta modalidad de residencia se
realiza con convocatorias muy definidas que son publicadas en el transcurso de
todo el año, en la sección convocatorias de la página web de mARTadero.</p>

