<p><img src="<?php echo $this->staticUrl('/break.jpg') ?>" class="box_image"/>
<strong>Los chicos de Break Dance &ldquo;Al Límite Crew&rdquo;</strong>
enseñan los valores de la música, el ejercicio y sobre todo cómo pasarlo bien
