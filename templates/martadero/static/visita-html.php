<p>Durante la visita te explicaremos cómo nació el proyecto mARTadero, qué
hacemos, recorreremos las salas, los proyectos, &hellip; <strong>y te haremos
partícipe de nuestros sueños</strong>.</p>

<p>También <strong>se organizan actividades para los más pequeños</strong> en
