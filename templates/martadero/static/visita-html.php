<h1>Visita guiada del proyecto mARTadero</h1>

<p>Si quieres <strong>conocer el proyecto mARTadero, su historia, su presente y
su futuro</strong>, no dudes en <strong>contactar con nosotros</strong>.</p>

<p><img src="<?php echo $this->staticUrl('/expo-historia.jpg') ?>" /></p>

<p><img src="<?php echo $this->staticUrl('/taller.jpg') ?>" class="box_image" />
Realizamos <strong>visitas guiadas para colegios</strong>, instituciones
organizaciones o incluso grupos privados de personas que quieran conocer nuestra
historia, nuestro trabajo y cómo hacemos realidad el sueño de tener un espacio
abierto al intercambio multicultural y las expresiones artísticas más
innovadoras.</p>
<p>Recibimos visitas principalmente los <strong>viernes de 9:00 a 12:00
horas</strong> en donde hacemos un <strong>recorrido por las salas</strong>,
explicamos el <strong>valor patrimonial del espacio</strong> y <strong>enseñamos
a los visitantes el valor de la cultura en la construcción de una sociedad más
justa y equitativa</strong>.</p>
<p>A lo largo de la visita realizamos actividades culturales sin ningún gasto
para los colegios además de <strong>ofrecer un rico refrigerio</strong>
auspiciado gentilmente por <a href="http://www.sofia.com.bo/webs/" target="_blank">Sofia</a>,
Nordland e Ilva, empresas amigas del mARTadero.</p>
<p>Las actividades culturales intentan ofrecer una <strong>visión general de la
potencialidad del espacio</strong>, siendo así que presentamos
<strong>títeres</strong>, <strong>obritas de teatro</strong>,
<strong>videos</strong> o a <strong>nuestros amigos de Break Dance &ldquo;Al
Límite Crew&rdquo;</strong>.</p>

<p><img src="<?php echo $this->staticUrl('/teatro.jpg') ?>" class="box_image" />
Obra de Globoflexia y Clown de la mano de &ldquo;Tio Globos&rdquo;.</p>
