Desde el proyecto mARTadero apoyamos e impulsamos la
<a href="http://es.wikipedia.org/wiki/Cultura_libre" target="_blank">Cultura Libre</a>,
convencidos del bien común que <strong>genera accesibilizar</strong> abierta y
libremente los contenidos culturales.</p>
<p>Porque es <strong>básico para el desarrollo justo y sostenible</strong> de
una sociedad, y porque producir éticamente es una responsabilidad social ante
dicha sostenibilidad.</p>
<p>Comunicar sigue siendo "hacer a otro partícipe de lo que uno tiene", más aún
viviendo en la actual sociedad del conocimiento, donde todos podemos ganar al
compartirlo.</p>
<p><strong>¡Suscribimos esa nueva -y antigua- lógica de comunicar, de hacer
común lo bueno!</strong></p>
<p>En Bolivia los beneficios de apoyar la Cultura Libre se verían directamente
reflejados en:</p>

<ul>
    <li>Una <strong>mayor visibilidad de las propuestas culturales</strong>,
    demostrando que el talento de las creaciones locales impera sobre lógicas de
    distribución que no favorecen al país y la región.</li>
    <li><strong>Acceder a una mayor cantidad de productos artísticos</strong>
    pudiendo así compartir, intercambiar, modificar y aprender de un gran fondo
    documental al que hoy en día no es posible su acceso.</li>
    <li><strong>Generación de redes locales</strong> de jóvenes creadores
    convencidos de la libertad que supone tener el poder de distribución y de
    esta manera ser un grupo homogéneo con capacidad de negociación ante la
    empresa privada y el sector público.</li>
    <li><strong>Hablar desde una realidad glocal</strong>, ya que el paradigma
    socio-económico promovido por las grandes corporaciones culturales ha
    quedado obsoleto. Creemos necesario el fomento de este nuevo modelo que
    genera mayores oportunidades y posibilidades de desarrollo artístico y
    social.</li>
</ul>

<h3>Por una Comunicación Libre</h3>
<p><a href="<?php echo $this->url(array('page' => 'martadero-v20.html'), 'base_static') ?>">Consulta la proyección del mARTadero 2.0 y las lógicas de Comunicación</a></p>
<p>Desde el proyecto mARTadero entendemos la <strong>necesidad de difundir
libremente contenidos culturales al tiempo que se defienden y fomentan los
derechos morales de los creadores.</strong></p>
<p>Esta necesidad parte por cambiar de paradigma, de plantear el
<em><strong>talento</strong></em> <strong>como validador
artístico-cultural</strong> a través de la posibilidad de <strong>compartir y
difundir el pensamiento contemporáneo libremente</strong>, <strong>rompiendo así
con las constricciones socioeconómicas propias de países como
Bolivia</strong>.</p>
<p><strong>Se ha iniciado un cambio</strong> en el mundo artístico y cultural
que está llamado a revolucionar la forma en que hemos entendido la difusión y
comercialización de productos culturales hasta hoy, ya que <strong>las nuevas
tecnologías nos han configurado como sociedades colectivas</strong> y solo es
entendible su uso como productoras de tal colectividad.</p>
<p><strong>El acceso libre y abierto a la cultura</strong> fortalece las
relaciones sociales y comunitarias, además de los procesos democráticos, y
<strong>se configura como base para el desarrollo justo y sostenible de la
sociedad</strong>. Por ejemplo, en lo económico, sobre todo en lo que respecta a
los creadores, el tema no es cómo ganar dinero o no, sino cómo está estructurado
el sistema económico en torno a la autoría de contenidos intelectuales y las
condiciones sociales y culturales de reconocimiento. Por ello, <strong>el reto
de producir y consumir contenidos en formas más éticas se convierte en una
responsabilidad social para encontrar la forma en que sea económicamente
sostenible</strong>. Sin embargo, se precisa para ello el desarrollo y evolución
de una serie de fundamentos, de ideas y de valores a partir del progreso de la
razón, el compromiso y la corresponsabilidad. Ahora, <strong>es responsabilidad
del artista contemporáneo lograr posicionarse y responder a la realidad
contextual en la que vive</strong>.</p>
<p><strong>"Otra sociedad del conocimiento es posible"</strong>, podemos afirmar
parafraseando la famosa máxima alter mundialista. Una sociedad más democrática,
libre y donde la cooperación y la colaboración entre los ciudadanos sean dos
palabras que se respiren a diario.</p>
<p>Según la <em><a href="http://es.wikipedia.org/wiki/Cultura_libre" target="_blank">Definición
de las obras culturales libres</a></em>, son trabajos libres aquellos que
permiten las siguientes libertades:</p>

<ul>
    <li>usar el trabajo y disfrutar de los beneficios de su uso.</li>
    <li>estudiar el trabajo y aplicar el conocimiento adquirido de él.</li>
    <li>hacer y redistribuir copias, totales o parciales, de la información o
    expresión.</li>
    <li>hacer cambios y mejoras, y distribuir los trabajos derivados.</li>
</ul>

<p>Desde el mARTadero <strong>trabajamos por difundir Arte y Cultura para el
Cambio Social</strong>, en este sentido realizamos entrevistas, artículos y
documentos que pretenden ser un <strong>aporte al acercamiento del público a la
cultura contemporánea boliviana</strong>, todo ello por supuesto, de libre
acceso y difusión.</p>

