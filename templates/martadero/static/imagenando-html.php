<h2>Sobre el modo de participar</h2>
<p>Si sabes de otros personajes que estuvieron o debieron estar en el mARtadero,
tienes varias formas de participar en esta reconstrucción histórica
participativa que llevamos a cabo:</p>

<h3>POSIBILIDAD 1. NOMBRE</h3>
<p>Escribe acá abajo el nombre del personaje, o postéalo en el blog citado.</p>
<p>Nuestros investigadores hemerográficos y encargados de reconstrucción
histórica encontrarán la evidencia fotografica y establecerán las circunstancias
de la estancia de dicho personaje y se imprimirán.</p>

<h3>POSIBILIDAD 2. TEXTO</h3>
<p>Si lo deseas, investiga en qué circunstancias y por qué estuvo acá, y redacta
un resumen de ello en un máximo de 1.000 caracteres (con espacios), posteándolo
en el blog.</p>
<p>Nuestros investigadores hemerográficos encontrarán la evidencia fotográfica
de la estancia de dicho personaje y se imprimirá con el texto, citando tu
nombre.</p>

<h3>POSIBILIDAD 3. IMAGEN</h3>
<p><a href="<?php echo $this->staticUrl('/b_einstein.jpg') ?>" class="highslide right_image" onclick="return hs.expand(this);">
<img src="<?php echo $this->staticUrl('/b_einstein-small.jpg') ?>" /></a>
Si lo deseas y puedes, saca a la luz una imagen de dicho personaje en el
mARTadero, y postéala en el mismo blog (formato JPG, 30x30 cm, 100 dpi).</p>
<p>Nuestros encargados de reconstrucción histórica establecerán las
circunstancias en que dicha foto se realizó, y se imprimirá con la imagen,
citando tu nombre.</p>

<h3>POSIBILIDAD 4. IMAGEN y TEXTO</h3>
<p>Si lo deseas y puedes, saca a la luz una imagen de dicho personaje en el
mARTadero, e investiga en qué circunstancias y por qué estuvo acá, cumpliendo
los requisitos arriba citados, y postea todo en el mismo blog. Nuestros
diagramadores comprobarán la veracidad, lo armarán en el formato oficial, y se
imprimirá citando tu nombre.</p>

