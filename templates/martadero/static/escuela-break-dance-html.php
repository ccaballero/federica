<h2>COSTO Y REQUISITOS</h2>
<p>El costo de la matrícula será de <strong>50 Bs. mensual y 80 Bs. los dos
horarios</strong>.</p>
<p>Los alumnos menores de edad deberán venir acompañados de un tutor para firmar
un documento de consentimiento. Para su protección, <strong>deberán ir provistos
de coderas, muñequeras, rodilleras, etc</strong>. No obstante,
<strong>proveeremos de dicho material a los estudiantes hasta que puedan
adquirir los propios.</strong></p>
<p>Los cursos tienen los módulos de: <strong>Foundations, Power Moves,
Footworks, Toprocks y Freezes</strong>.</p>

