<h1>Escuela de Break Dance "Breaking the Floor"</h1>

<p><img src="<?php echo $this->staticUrl('/logo-breaking-the-floor.jpg') ?>" /></p>

<p>El <strong>Break Dance (o también llamado B-Boying) es una danza urbana que
forma parte integral de la cultura hip hop</strong> surgida en las comunidades
afroamericanas y latinoamericanas. A pesar del hecho que muchas personas en el
mundo lo practican, en Bolivia (especialmente Cochabamba), la influencia de este
movimiento es más que reciente. Ahondando más en el asunto, <strong>uno de los
principales obstáculos</strong> en la expansión de este arte, <strong>residen en
la envidia de muchos de los grupos a la hora de compartir sus conocimientos y
experiencias</strong> al respecto. Esto conlleva a una práctica autodidacta, o
en el peor de los casos, a la perdida de interés.</p>

<p><a href="<?php echo $this->staticUrl('/escuela-break-dance-1.jpg') ?>" class="highslide box_image" onclick="return hs.expand(this);">
<img src="<?php echo $this->staticUrl('/escuela-break-dance-1-small.jpg') ?>" /></a>
También es cierto que<strong> no muchos se sienten capaces de llevarlo a
cabo</strong>, puesto que para ello son necesarias diversas cualidades que
algunos ven imposibles de alcanzar. Este es el caso del equilibrio, la
templanza, la fuerza...pero sobre todo el compromiso.</p>
<p><strong>Con el compromiso como aliciente, el miedo y otros factores
desalentadores al momento de la práctica, apenas e importan.</strong></p>
<p>Un <strong>sólido interés por aprender es más que suficiente para realizar
cualquier movimiento</strong>, por más complicado que este parezca.</p>
<p>El <strong>Break Dance</strong>, mediante los diversos estilos, movimientos y
combinaciones, <strong>fomenta una disciplina personal muy
saludable</strong>.</p>
<p>Los grupos, casi en su totalidad, entrenan o practican en las plazas y
plazuelas de la ciudad. <strong>Muchas veces la falta de un lugar
específico</strong> para desenvolverse, es el motivo para la búsqueda de nuevas
opciones.</p>
<p><a href="<?php echo $this->staticUrl('/escuela-break-dance-2.jpg') ?>" class="highslide box_image" onclick="return hs.expand(this);">
<img src="<?php echo $this->staticUrl('/escuela-break-dance-2-small.jpg') ?>" /></a>
Como solución a este problema, es que <strong>inauguramos la primera Escuela
de Baile Urbano en Cochabamba</strong>, teniendo como base el Break Dance con
sus diferentes estilos y ramificaciones.</p>
<p>La escuela se desarrolla en los ambientes del proyecto mARTadero, ubicado en
la calle 27 de Agosto y Ollantay (Ex Matadero).</p>
<p>La <a href="http://www.martadero.org/archivos_php/detalle_sala.php?id_amb=26">Sala de las Cabezas [E1]</a>,
o de expresión corporal contemporánea, <strong>es ideal para el desarrollo de
los talleres</strong> y está disponible para grupos de danza o baile que deseen
reservarla para ensayos o presentaciones.</p>
<p>Para solicitudes <a href="http://www.martadero.org/php/enrutador.php">pueden hacerlo directamente desde aquí</a>.</p>

