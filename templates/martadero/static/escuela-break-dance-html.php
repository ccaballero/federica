<h2>SOBRE "DESTROYING THE FLOOR"</h2>
<p>Desde 2010 la escuela "Breaking the Floor" organiza el <strong>concurso
internacional Break Dance "Destroying the Floor"</strong>.</p>
<p>Además de enseñar el arte, este evento genera movimiento interactuando en
distintas locaciones de la ciudad con el público y vecinos de algunos lugares.
De esta forma se apoya a eventos tanto urbanos como artísticos en general.</p>
