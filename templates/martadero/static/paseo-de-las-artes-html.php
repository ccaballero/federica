<h2>Plaza de las Artes &gt; <a href="http://plazaartes.wordpress.com" target="_blank">http://plazaartes.wordpress.com</a></h2>
<p>El proyecto consiste en la generación de un proceso de participación
ciudadana que impulse la rehabilitación, resignificación y acondicionamiento de
la Plaza de los Arrieros de Cochabamba (Bolivia), para convertirla en la Plaza
de las Artes, un espacio donde se desarrolle la creatividad, que sea punto de
encuentro para la gente y donde se pueda combinar recreación, diversión, ocio y
descanso. Un proyecto prototípico de interacción en un espacio urbano social. A
través de este proceso se generará una reflexión y una concienciación de los
vecinos del barrio que llevará a un progresivo mejoramiento de su contexto
vital.</p>
<p>Este proyecto cuenta con el apoyo de
<a href="http://www.emprendeduriacultural.net/ejecentral/" target="_blank">Eje
Centr4l</a>.</p>

<p><a href="http://www.emprendeduriacultural.net/ejecentral/" target="_blank">
