<h4>Pecha Cocha 002</h4>
<p><strong>Artistas para la trans-formación</strong></p>
<p>Todas las disciplinas creativas se confrontan hoy con la aceleración de la
hipermodernidad.</p>
<p>Las técnicas evolucionan, los contenidos se mezclan y las relaciones entre el
creador y el mundo son cada vez más articuladas. Cada día nacen nuevas
herramientas y se desarrolla un acceso más facilitado a los medios de producción
artística.</p>
<p>La frontera entre productores y consumidores de arte ya es un margen
difuminado dentro del que se mueve un número enorme de personas. Profesiones
creativas como el diseño gráfico o el web design reivindican cada vez más su
lugar al lado de las artes más tradicionales y reconocidas.</p>
<p>El trabajo del creador ya no se limita a un proceso de investigación
individual, sino que se desarrolla dentro de un continuo proceso de formación.
El aprendizaje constante por un lado, y por el otro las practicas de enseñanza a
las que muchos artistas se dedican tienen un potencial revolucionario en
nuestras sociedades.</p>
<p>Formación, trans-formación y transformación son a la vez elementos de nuestro
tiempo y de nuestro futuro.</p>
<p>Como pasa muy a menudo los consideraremos como un fenómeno social y un
estimulante desafío artístico.</p>

