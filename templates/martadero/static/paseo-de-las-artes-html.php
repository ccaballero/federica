<p><strong>Cada participante dispone de 20 imágenes (al estilo de las
presentaciones Power Point) y debe dedicar 20 segundos a cada una para contar
algo de su obra, de su trabajo y de su visión.</strong></p>

<p>Gracias a la brevedad del formato, en cada encuentro es posible conocer el
trabajo de varios creadores y disponer de un espacio de discusión libre y
dinámico.</p>

<p>Es una forma de crecer juntos, y de hacer que el ambiente cultural de la
ciudad crezca con nosotros.</p>

<p><strong>Artistas visuales, diseñadores gráficos, arquitectos, o cualquier
persona quiera compartir una experiencia creativa con los demás están invitados
