<h3>Objetivos generales</h3>
<p>El objetivo más amplio del proyecto es, dentro de este marco, la generación
de un espacio público plural, donde las manifestaciones de diversidad sean un
elemento enriquecedor y no un factor excluyente.</p>
<p>A través de la premisa de la democracia cultural, convertir a la Plaza de las
Artes en un punto de encuentro, de convivencia, de fraternidad y de diálogo, y
de esta forma unirla con el proyecto mARTadero, con otros posibles proyectos
futuros y con actividades socioculturales varias, para que las formas de
expresión de los pobladores encuentren un espacio de representación, concreción,
libre expresión, difusión e intercambio, educando en la comunicación y el
disfrute cultural, y sin imponer ideologías o formas de pensamiento.</p>

<h3>Propósito</h3>
<p>Reconstruir un sentido de pertenencia comunitario y responsabilidad colectiva
en el barrio de Villa Coronilla impulsando el encuentro y el intercambio de
ideas entre los vecinos.</p>

<h3>Objetivos Específicos</h3>
<ul>
    <li>Generar un sistema constante de recogida de la memoria histórica del
    barrio.</li>
    <li>Activar un flujo masivo de comunicación entre vecinos sobre los
    problemas del barrio.</li>
    <li>Impulsar el nacimiento de una estructura que permita la construcción
    colectiva de modelos de futuro por parte de los vecinos, como plataforma
    material y organizativa para activar el diálogo y el intercambio de ideas
    entre los habitantes del barrio de Villa Coronilla.</li>
</ul>

