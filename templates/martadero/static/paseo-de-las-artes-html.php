<h1>Paseo de las Artes</h1>

<p>El Paseo de las Artes consiste en establecer un circuito enlazando los
espacios públicos que tenemos en nuestro barrio, en este recorrido crear
representaciones artísticas urbanas; estas ilustrarán la historia, las
actividades y vivencias del barrio.</p>
<p>Al mismo tiempo funcionará como atracción de flujos humanos moviéndolos del
centro de la ciudad hacia nuestros barrios.</p>
<p>Los espacios públicos como plazas y parques serán lugares donde se realicen
actividades artísticas-culturales, donde las personas puedan pasar un buen rato
y compartir con sus vecinos.</p>

