<h4>Pecha Cocha 001</h4>
<p>La ciudad es la estructura social humana que con mayor facilidad sobrevivirá
al tercer milenio.</p>
<p>El entorno urbano se ha convertido en el verdadero hábitat del ser humano,
conociendo en los últimos años un crecimiento exponencial en todo el mundo. El
concepto de "comunidad" ha sido sustituido por una "sociedad" hecha de
individuos anónimos y diferentes, dentro de una estructura compleja y
evolutiva.</p>
<p>El espacio público urbano (sea físico o virtual) representa hoy ese nodo en
el que las personas se encuentran y entran en interacción con lo artificial.</p>
<p>Todas las actividades creativas se confrontan hoy como nunca con el contexto
urbano, con la complejidad de su estructura, con la variedad del catálogo de su
habitantes, con la imprevisibilidad de los fenómenos y de los eventos que se
desarrollan en ello.</p>
<p>Como testigo de la realidad, activador de dinámicas de interacción, como
coordinador de procesos de cambio o simplemente como invasor del espacio urbano,
el creador contemporáneo se relaciona con todo el material tangible e intangible
que la ciudad le ofrece.</p>

