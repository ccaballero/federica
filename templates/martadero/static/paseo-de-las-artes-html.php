<h2>&gt;&gt; REMEZCCBA &gt; <a href="http://remezcbba.wordpress.com/" target="_blank">http://remezcbba.wordpress.com/</a></h2>
<p><a href="http://remezcbba.wordpress.com/" target="_blank" class="box_image">
<img src="<?php echo $this->staticUrl('/ha-llegado2.png') ?>" /></a>
Buscando realizar una apropiación lúdico-artística del espacio urbano,
específicamente de la calle Ollantay (Villa Coronilla, costado de la "Plaza de
los arrieros") fue que se programó "Remezcbba 2010" festival basado en el uso
público como lugar de encuentro, integración y de expresión creativa, realizado
el día 06 de septiembre durante el "día del peatón" destinado a la no
circulación vehicular en la ciudad de Cochabamba. Esta jornada comenzada durante
la primera mitad del día y finalizada con el anochecer, contempló: Kermesse,
exposición de propuestas de rehabilitación de la plaza de los arrieros, Live
Painting, intervención de pintura colectiva sobre el muro sur del ex matadero
municipal, exhibición de deportes urbanos, break dance, rap, música en vivo,
instalaciones audiovisuales, intervenciones callejeras literarias y set para
fotografías familiares.</p>
