<p>Los <strong>espacios del proyecto mARTadero son un recurso valioso para el
desarrollo de las artes</strong>, de la cultura y de todas las actividades
creativas en la ciudad de Cochabamba.</p>
<p>Las prácticas diarias del encuentro, de la integración, del compartir
experiencias e ideas han formado una costumbre de trabajo que <strong>en cinco
años ha convertido el mARTadero en un proyecto de referencia en Bolivia y en el
continente Sudamericano</strong>.</p>
<p>En un mundo hecho de bloques y dogmas, donde arte y cultura siguen siendo
factores discriminantes, en el Proyecto mARTadero se trabaja para que sean los
impulsores más fuertes de la inclusión y la integración social.</p>
<p>Convencidos del poder detonante que tienen las ideas cuando pueden ser
compartidas libremente, el departamento de Acción Urbana de Fundación Imagen
junto con el área de arquitectura y diseño del Proyecto mARTadero promueven las
primeras <strong>Noches de Pecha Kucha de Bolivia</strong>.</p>

<p><strong>El Pecha Kucha es una forma para intercambiar experiencias creativas de forma
libre e interactiva</strong>. Nació en el 2003 en Tokio como un formato rápido y
dinámico con el cual diseñadores, creadores y arquitectos podían enseñar su
trabajo a los demás, intercambiando opiniones y generando redes de contactos
entre ellos.</p>

<p>
