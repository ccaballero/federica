<h2><a name="arcoiris"></a>SISTEMA ARCOIRIS</h2>
<h3>Estrategia de sostenibilidad</h3>
<p><img src="<?php echo $this->staticUrl('/../tarjeta-arcoiris-html/tarjeta-arcoiris.gif') ?>" class="box_image" />
El proyecto mARTadero, para su sostenibilidad, ha diseñado una estrategia basada
en:</p>
<ul>
    <li><strong>Cooperación mediante patrocinio</strong>, auspicio, apoyo y
    colaboración.</li>
    <li>Un sistema &ldquo;arco iris&rdquo; de adhesiones, con <strong>tarjetas
    para distintas realidades y bolsillos</strong>.</li>
    <li><strong>Un aporte &ldquo;fondart&rdquo;</strong> interno (fondo
    concursable para el desarrollo de las artes) del 10% de todos los ingresos
    generados en y por el proyecto, incluyendo entradas, productos y
    salarios.</li>
    <li><strong>Una contribución acordada segun el uso de las instalaciones para
    actividades culturales</strong>, que cubre consumo eléctrico, agua, servicio
    de seguridad y limpieza.</li>
</ul>

