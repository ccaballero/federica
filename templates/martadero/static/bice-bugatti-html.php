<h2>EL ARTE CONTEMPORÁNEO BOLIVIANO TRASPASA FRONTERAS</h2>

<p><a href="http://www.bicebugatticlub.eu" target="_blank" class="box_image">
<img src="<?php echo $this->staticUrl('/logo-bice-bugatti.jpg') ?>" title="Bice Bugatti Club" /></a>
Tras la firma, a comienzos de 2009, de un convenio entre proyecto mARTadero y la
Alcaldia de Nova Milanese-Italia, por mediación de la <strong>Asociación
Cultural Bice Bugatti Club</strong>, se dio origen a &ldquo;Proyecto
Bolivia&rdquo;, instancia destinada a generar un diálogo e intercambio artístico
entre ambos países; debido a esto fue que para la versión número 50 del concurso
pictórico &ldquo;Bice Bugatti&rdquo;, la asociación cultural italiana, creó
<strong>una categoría especialmente dedicada a la participación de artistas
bolivianos</strong> dentro del certamen.</p>
<p>Hasta la fecha se han convocado a artistas de Bolivia, pero además del sur de
Perú, norte de Chile, y norte de Argentina, dentro del marco de
<a href="<?php echo $this->url(array('page' => 'centros-del-sur.html'), 'base_static') ?>">Territorios
Culturales del Sur</a>.</p>

<p>Aqui puedes leer un poco más (en italiano) desde la página del Bice Bugatti
Club: <a href="http://www.bicebugatticlub.eu/eventi/eventi_det.cfm?newsID=47" target="_blank">www.bicebugatticlub.eu</a></p>

<p>Te invitamos a visitar el <a href="http://www.museonova.com/" target="_blank">Museo
Virtual de Nova Milanese</a>.</p>

