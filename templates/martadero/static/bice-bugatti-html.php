Proyecto <strong>mARTadero</strong> fue el <strong>encargado de convocar y luego
seleccionar entre más de una treintena de obras</strong>, las que finalmente
serían expuestas en competencia el día 14 de junio en uno de los concursos de
más larga trayectoria en el país europeo.</p>

<p>La curaduría se tituló:</p>

<blockquote>Cuerpo individual y cuerpo social. Conflictos generadores.</blockquote>

<p><a href="<?php echo $this->baseUrl('/media/doc/Curaduria-2009-Cuerpo-individual-y-cuerpo-social-conflictos-generadores.doc') ?>">[descárgatela en word]</a></p>

<p>De esta manera, <strong>las propuestas de los artistas</strong>:
<strong>Erika Ewel</strong>, <strong>Elmarsh</strong>, <strong>Magenta
Murillo</strong>, <strong>Mauricio Sánchez Patzy</strong>, <strong>Pablo
Villagomez</strong>, <strong>Alejandro Sejas Crespo</strong>, <strong>Daniela
Rico</strong> y <strong>Moisés Anturiano</strong>, fueron las elegidas para
viajar a Italia, a dar muestra de lo que actualmente se está desarrollando en
términos plásticos dentro de nuestro país, resultando favorecidas con un premio
consistente en $us 1000, más la permanencia en la colección &ldquo;Bice
Bugatti&rdquo;, compuesta por <strong>los trabajos ganadores</strong> en las
versiones anteriores del concurso, las obras: &ldquo;Sangre de pez&rdquo; de
<strong>Alejandro Sejas Crespo</strong> y &ldquo;Domingo de invierno&rdquo; de
<strong>Moisés Anturiano</strong>.</p>

<p>Es necesario destacar que, tanto los trabajos  ganadores como todos los
seleccionados, luego de ser expuestos en Nova Milanese, serán exhibidos en el
Festival Latinoamericano de Milán y posteriormente en la ciudad de Bergamo.</p>

