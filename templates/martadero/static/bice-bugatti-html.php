del proyecto mARTadero, no viene más que a <strong>fortalecer las relaciones que
mantiene este espacio cultural con sus homónimos, dentro y fuera del
continente</strong>, basadas en sus 7 principios básicos: innovación,
investigación, experimentación, rigor conceptual y formal, integración,
intercambio e interculturalidad.</p>

