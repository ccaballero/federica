<h3>Texto curatorial de Alessia Barzaghi sobre obras italianas</h3>
<p>Cruzando las fronteras y cambiando el horizonte, los puntos cardinales, los
espacios de referencia físicos y mentales, varían el lenguaje y las cosas que
éste nombra.</p>
<p>Ocurre un movimiento que pone en cuestión la relación entre los signos y los
conceptos a esos destinados por su cultura. El conocimiento de la persona oscila
entre el &ldquo;background&rdquo; cultural que le pertenece a él, el resultado
del lugar y el tiempo en que vive, y las imágenes y los significados
inesperados, se desliza hacia una visión alternativa de la realidad que le rodea
y hacia un nuevo sistema de comunicación.</p>
<p>Aquí, entonces, que el espacio en que el pensamiento se mueve crece, al igual
que los caminos transitables, fortaleciendo en la mente el privilegio de la duda
y de la elección, de una pluralidad de visiones en las que cada uno apela a su
propia sensibilidad&hellip;
