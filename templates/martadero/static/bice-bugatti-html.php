<h3>Texto curatorial de Fernando García sobre obras latinoamericanas</h3>
<p>Los artistas no son un tipo especial de personas, sino que cada persona somos
un tipo especial de artista, afirmaba acertadamente la <em>entidad transpersonal
post situacionista</em> Hakim Bey. El ser humano es creativo por naturaleza y
así, al crear, nos creamos a nosotros mismos y creamos el cosmos que nos rodea.
Un cosmos a la vez complejo y esencial, formado unas veces de palabras
ininteligibles, otras veces de referencias sutiles, y otras de imágenes
enigmáticas. Signos y símbolos que configuran nuestro tiempo y nuestro espacio
de forma dinámica, en una realidad que se desplaza, buscando continuamente
sentido a través de nuevos horizontes, exteriores e interiores&hellip;
