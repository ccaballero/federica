<h1><a name="responsables-areas"></a>Coordinación de áreas artísticas</h1>
<p>El equipo humano del proyecto mARTadero está formado por un grupo de
coordinadores para cada área artistica.</p>

<h2>Artes visuales y fotografia</h2>
<p><img src="<?php echo $this->staticUrl('/artes-visuales.jpg') ?>" class="box_image" />
<strong>Dennis Salazar</strong><br />
artesvisuales[a]martadero.org</p>
<p>Los criterios de evaluación que determinan el uso de sus espacios son los
mismos principios que rigen para la asociación NADA y su proyecto mARTadero:
Innovación; Investigación; Rigor conceptual y formal; Integración; Intercambio
de conocimientos y experiencias; e Interculturalidad.</p>

<h3>OBJETIVO GENERAL</h3>
<p>Promover el arte contemporáneo.</p>

<h3>OBJETIVOS ESPECIFICOS</h3>
<ul>
    <li>Realizar exhibiciones de artes visuales.</li>
    <li>Implementar seminarios relativos a las artes visuales.</li>
    <li>Elaborar talleres artísticos.</li>
</ul>

<h2>Letras</h2>
<p><img src="<?php echo $this->staticUrl('/letras.jpg') ?>" class="box_image" />
<strong>Juan Malebrán</strong><br />
letras[a]martadero.org</p>
<p>El área de letras de proyecto mARTadero tiene como principal objetivo
promover la creación, la difusión y el intercambio de propuestas literarias, a
través de talleres, producción y participación en actividades ligadas a la
escritura, tanto a nivel nacional como internacional.</p>

<h2>Artes escénicas</h2>
<p><img src="<?php echo $this->staticUrl('/escenicas.jpg') ?>" class="box_image" />
<strong>Viola Vento</strong><br />
artesescenicas[a]martadero.org</p>
<p>El área de Artes Escénicas del Proyecto mARTadero tiene el objetivo de
impulsar la formación de creadores y la producción actividades relativas a las
artes escénicas (Teatro, Danza, Títeres, etc.) desde el espacio mARTadero,
encaminando estas actividades hacia la profesionalización de los artistas y
gestores dentro su campo.</p>
<p>En correspondencia a lo  planteado el área se encarga de promover y producir
actividades formativas, de creación y de muestra de distintas tendencias y
géneros dentro las artes escénicas. Las propuestas de actividad se evalúan y
llevan a cabo en correspondencia a los principios que rigen para la asociación
NADA y su proyecto mARTadero.</p>

<h2>Diseño gráfico y arquitectura</h2>
<p><img src="<?php echo $this->staticUrl('/arqui-diseno-graf.jpg') ?>" class="box_image" />
<strong>Eliana Ramírez</strong><br />
arquitectura[a]martadero.org</p>
<p>El área de arquitectura y diseño grafico busca contribuir al desarrollo
grafico y arquitectónico en Cochabamba y en el país, a través del impulso de
cursos, proyectos, encuentros, redes e intervenciones.</p>
<p>Actualmente se encarga de la progresiva recuperación del sitio, poniendo en
valor su aspecto patrimonial y adecuando los ambientes para el uso cultural y
artístico. Igualmente trabaja en la preservación y recuperación del patrimonio.
En diseño se realizan propuestas para el espacio y sus actividades, apoyando
producción y difusión. Además el área agrupa a profesionales de la arquitectura
y del diseño grafico interesados en el trabajo artístico interdisciplinario.</p>

<h2>Audiovisual</h2>
<p><img src="<?php echo $this->staticUrl('/audiovisual.jpg') ?>" class="box_image" />
<strong>Bernard Marinovitch</strong><br />
audiovisual[a]martadero.org</p>
<p>Además de encargarse de la gestión de actividades audiovisuales en el sitio,
el área pretende impulsar la producción audiovisual profesional y experimental
en nuestro medio. El área se encarga de promover y producir actividades
creativas, formativas y expositivas de diversos géneros y tendencias. Una
prioridad para el área es el trabajo multidisciplinar, durante estos primeros
años en el mARTadero se han desarrollado varios trabajos conjuntos con otras
áreas. Apostamos por la exploración del lenguaje audiovisual y por la
composición de lógicas de producción alternativas adecuadas para nuestro
contexto ecónomo-cultural.</p>

<h2>Música</h2>
<p><img src="<?php echo $this->staticUrl('/musica.jpg') ?>" class="box_image" />
<strong>Diego Arandia</strong><br />
musica[a]martadero.org</p>
<p>Promueve, difunde, facilita y asesora la producción de eventos musicales
(conciertos, talleres, etc.). Prioriza y concentra su trabajo en la música de
artistas nuevos, especialmente de aquellos que experimentan el camino de la
composición y que por razones diversas no pueden acceder a espacios para la
expresión de su arte y talento. No se prefiere ni discrimina ningún género
musical en franca correspondencia con sus principios de libertad y democracia.
</p>

<h2>Interacción Social</h2>
<p><img src="<?php echo $this->staticUrl('/interaccion-social.jpg') ?>" class="box_image" />
<strong>Susana Obando</strong><br />
interaccionsocial[a]martadero.org</p>
<p>Desde este área se trabaja para impulsar procesos de desarrollo social a
través de estrategias artístico-culturales, generando un movimiento progresivo
de estrategias de futuro basadas en la creatividad.</p>

