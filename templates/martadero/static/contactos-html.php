<h2><a name="envio"></a>Para hacer un envío</h2>
<p>Si vas a enviarnos un paquete debes tener en cuenta las siguientes
especificaciones:</p>

<ul>
    <li>El envío se realizará <strong>a nombre de Fundación Imagen</strong>.</li>
    <li>En lo posible, <strong>los paquetes no pesarán más de 2 Kg</strong>
    <strong>y su valor declarado no deberá estar por encima de los 99 US$</strong>.
    Es preferible mandar diversos paquetes que cumplan dichas condiciones cada
    uno.</li>
    <li>Se realizará <strong>una declaración del remitente de que es material
    cultural</strong>, asegurándose de que en <strong>la casilla 36 del
    certificado de origen se indique que se trata efectivamente de material
    cultural</strong>, para acogerse así al <strong>convenio internacional
    (AC36)</strong></li>
</ul>

