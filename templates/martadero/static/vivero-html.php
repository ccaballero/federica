<h2>¿Qué es el VEAC?</h2>
<p>Es un programa del mARTadero que apoya a los artistas y no artistas,
emprendedores. Brindándoles la posibilidad de utilizar su creatividad para
generar una estrategia integral que contribuya al desarrollo económico y social
en Bolivia a través del arte y la cultura.</p>
<p>El VEAC fomenta la generación y surgimiento de nuevas empresas, a las que
provee de soporte de espacio, técnico, asesoramiento en marketing, ventas y
comercialización; asesoramiento legal, contable e impositivo, consultorías
específicas, acceso a laboratorios, cursos y seguimiento Modelos de Negocio,
apoyo en el seguimiento de la gestión, entre otros.</p>

<p>El <strong>mARTadero</strong>, a través de este Programa y el área de apoyo
de Autogestión, <strong>acompañan y asesoran</strong> los viveros.</p>
<p>El <strong>mARTadero,</strong> a través de este Programa de Vivero de
Emprendimientos Artístico-Creativos, ya <strong>acompaña y asesora</strong> el
vivero <a href="<?php echo $this->url(array('page' => 'itaca.html'), 'base_static') ?>">Itaca:
Café Cultural</a>.</p>

<h2>OBJETIVOS del VEAC</h2>
<ul>
    <li><strong>Promover el desarrollo económico del sector artístico y
    creativo</strong>.</li>
    <li><strong>Contribuir a la organización del sector cultural y
    creativo</strong>, y su reconocimiento como un ámbito que cada vez alcance
    mayor protagonismo en la dinámica económica mundial.</li>
    <li><strong>Contribuir y fomentar las prácticas</strong>, experiencias e
    iniciativas en el sector artístico y creativo, <strong>promoviendo la
    investigación</strong> y <strong>difusión</strong> de dicho conocimiento.
    </li>
    <li><strong>Contribuir</strong> al mejoramiento de <strong>la calidad de
    vida</strong> de <strong>artistas</strong> y <strong>creadores</strong>,
    mediante la posibilidad de construir empresa y generar empleo a partir de
    los productos y servicios que ofrecen.</li>
    <li><strong>Capacitar a los artistas</strong> para <strong>crear empresas
    artísticas y creativas</strong> y fortalecer las pocas existentes.</li>
    <li><strong>Brindarles un espacio</strong> con el
    <strong>equipamiento</strong> técnico y logístico para que consoliden su
    trabajo <strong>de manera profesional</strong>, fortaleciendo así la oferta
    cultural de calidad.</li>
    <li><strong>Dotar de las herramientas</strong> necesarias en cuanto a
    conocimientos relacionados con las diferentes <strong>áreas de la
    administración</strong> y <strong>gestión de empresas</strong> en el ámbito
    cultural y artístico.</li>
    <li><strong>Capacitar</strong> a los participantes en el
    <strong>uso</strong> <strong>de las nuevas tecnologías</strong> para el
    trabajo con redes de circulación y difusión de productos culturales
    internacionales y nacionales.</li>
</ul>

<h2>Áreas de Admisión</h2>
<p>Se contempla <strong>cualquier idea de negocio o emprendimiento
desarrollado</strong> que se extienda en uno o varios de los siguientes
eslabones de la cadena de valor de los sectores: Formación (academias, centros
de capacitación), Desarrollo (idea, investigación), Preproducción, Producción,
Postproducción, Exhibición y Distribución.</p>

<ul>
    <li><strong>Música</strong></li>
    <li><strong>Audiovisual</strong></li>
    <li><strong>Diseño Gráfico</strong></li>
    <li><strong>Artes Visuales</strong></li>
    <li><strong>Literatura y Letras</strong></li>
    <li><strong>Artes Escénicas</strong></li>
    <li><strong>Interacción Social:</strong> Incluye las iniciativas enfocadas a
    la producción de eventos, primordialmente destinados a una función social,
    en cuanto a divulgación, concientización, socialización, usando el arte y la
    creatividad como base. Incluyen proyectos de gestión cultural.</li>
    <li><strong>Modalidad abierta:</strong> En esta modalidad se incluyen todas
    las ideas de negocio para Industrias Creativas que no sean posibles de
    clasificar en las modalidades anteriores; sea: edición, software, TV y
    radio, diseño (moda, textil, interface, etc.), videojuegos, artesanías,
    arquitectura y publicidad.</li>
</ul>

<h2>¿Quieres entrar al VEAC?</h2>
<p>Es muy importante que leas las Bases para entrar, ya que el trabajo que
realizamos no es juego, porque trabajamos para que tu idea aterrice y se
viabilice dentro de tus posibilidades y las nuestras.</p>

<ul>
    <li>Pueden participar todos los <strong>artistas y no artistas,</strong>
    <strong>creativos</strong>, <strong>mayores de edad</strong>, que tengan una
    <strong>idea de emprendimiento o un emprendimiento en desarrollo</strong>,
    por lo que están convencidos y dispuestos a trabajar en su idea.</li>
    <li>El emprendedor <strong>se compromete a asistir a las sesiones grupales y
    específicas</strong>, a <strong>realizar las actividades que de ellas se
    deriven</strong> el avance para el desarrollo de la idea de negocio.
    <strong>La inasistencia sistemática y el incumplimiento
    injustificado</strong> por parte del emprendedor frente a las actividades
    que se le designen, <strong>son motivo de exclusión del Vivero y por tanto
    de sus beneficios</strong>.</li>
    <li><strong>El componente de asesorías grupales</strong>, si bien se
    desarrolla en aulas con la conducción de un experto por cada tema,
    <strong>no tiene certificación como actividad académica</strong>. El
    <strong>objetivo es que cada emprendedor aplique de manera práctica los
    conocimientos adquiridos y los ponga en función de la
    implementación</strong> de su futuro emprendimiento. Este es un Programa de
    emprendimiento y no de formación, por tanto NO ENTREGA CERTIFICADOS.</li>
    <li>Todos los <strong>procesos de asesoría y acompañamiento durante el
    desarrollo</strong> <strong>de los modelos de negocio son
    gratuitos</strong>.</li>
    <li>El Programa <strong>no entrega dinero en efectivo durante el desarrollo
    de la idea de negocio</strong>. Sin embargo el <strong>apoyo al
    emprendedor</strong> para la búsqueda <strong>de dichos recursos</strong>
    hace parte del acompañamiento.</li>
    <li>El Programa <strong>no adquiere ningún derecho sobre las ideas de
    negocio seleccionadas y se compromete a guardar confidencialidad sobre
    aquellas otras que no sean admitidas</strong>. De igual manera adquieren
    responsabilidad frente a terceros por las negociaciones que los
    emprendedores realicen durante su proceso de asesoría y acompañamiento.</li>
    <li>Para hacer efectiva la postulación de una idea se deberá llenar el
    formulario de la siguiente dirección:<br />
    Pincha en este <a href="https://docs.google.com/a/martadero.org/spreadsheet/viewform?hl=es&amp;formkey=dFpSbzNDYnlTQnlyMlYxalRYZGN5R2c6MQ#gid=0" target="_blank">FORMULARIO DE POSTULACIÓN</a>
    <strong>Las personas u equipos</strong> seleccionados <strong>entran a
    formar parte de los procesos de desarrollo</strong> de modelos de negocio.
    </li>
</ul>

