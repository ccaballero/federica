<p><img src="<?php echo $this->staticUrl('/banner-tallerinfantil.jpg') ?>" class="box_image" />
El <strong>Taller de Creatividad Infantil es un programa de desarrollo social
del proyecto mARTadero</strong> cuya función es proveer a las niñas y niños del
barrio y de la ciudad en general un espacio alternativo de sana recreación.
Nuestra apuesta es por generar un espacio accesible desde diferentes estratos
socio-culturales y económicos en donde además pueden desarrollarse como
individuos frente a la sociedad.</p>

<p><strong>El programa se sostiene con el
<a href="<?php echo $this->url(array('page' => 'colabora.html'), 'base_static') ?>" target="_blank">apoyo
de donaciones</a> y la participación colaborativa de jóvenes del interior de
Cochabamba</strong>, así como voluntarias y voluntarios de las diferentes
ciudades y países, y los padres de familia.</p>

<p>Además cuenta con el <strong>apoyo periódico de especialistas</strong> en las
diferentes áreas<strong> relacionados con el arte, la cultura, la pedagogía y la
educación medioambiental</strong>.</p>

<p>El trabajo del Taller de Creatividad Infantil está basado en cuatro pilares:
</p>

<h2>Medio Ambiente</h2>
<p><img src="<?php echo $this->staticUrl('/cuidando-plantas.jpg') ?>" class="box_image" />
Se busca aportar y mejorar las condiciones medio ambientales que se presentan en
el barrio donde está enmarcado el mARTadero: Villa Coronilla. Esto genera en las
niñas y niños una adquisición de habilidades y actitudes dentro de la
concientización y el cuidado medio ambiental. Proponemos una educación para la
acción tomando como áreas: la salud planetaria y problemas medioambientales,
reciclaje, cultivo y cuidado de plantas, instaurando prácticas pro ambiental, en
el niño y el hogar.</p>
<div class="clear"></div>

<h2>Libre Expresión</h2>
<p>Implica el desarrollo de la creatividad mediante técnicas plásticas y
artísticas (teatro, danza, pintura, canto), creando en los niños un vinculado de
ideas, expresándolas mediante las actividades realizadas en el Taller generando
un aprendizaje y continuo desarrollo personal libre y auténtico.</p>

<h2>Desarrollo personal</h2>
<p>Obtener en los niños una mejor salud emocional y una buena relación con su
alrededor; la forma adecuada para alcanzar esa salud emocional es proporcionar
libertad en ellos y fortificarla, mediante juegos lúdicos, juegos grupales,
trabajo en equipo y otras actividades en las que puedan relacionarse los
niños con sus pares.</p>

