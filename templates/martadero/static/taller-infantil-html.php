<h2>Ciudadanía</h2>
<p><a href="<?php echo $this->staticUrl('/sonrisa.jpg') ?>" class="highslide box_image" onClick="return hs.expand(this);">
<img src="<?php echo $this->staticUrl('/sonrisa-small.jpg') ?>" /></a>
Dar a conocer a los niños que son parte de la sociedad, que conozcan sus
derechos y obligaciones frente a la sociedad, formando un respeto como
individuos que forman parte de la ciudad para mejorar la calidad de vida del
individuo evitando cualquier tipo de exclusión frente a la sociedad. Generando
un cambio y concientización en los niños, familiares, el barrio y la ciudad.
</p>
<p>Todo el trabajo en el Taller de Creatividad Infantil está basado en la
corriente pedagógica constructivista y cooperativista teniendo constantemente y
a diario el apoyo escolar con la tutoría en los deberes escolares como tareas,
repasos, lectura, escritura, &hellip; motivando y realizando un trabajo paralelo
con los niños.</p>

