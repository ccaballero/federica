<h1>REDES CULTURALES<br />
Red Cultural Mercosur</h1>

<p>Desde 2004 el <strong>proyecto mARTadero</strong> forma parte de la Red
Cultural Mercosur.</p>

<p><img src="<?php echo $this->staticUrl('/rcm.jpg') ?>" /></p>

<p>La RCM en <strong>10 puntos</strong>:</p>

<ol>
    <li>La Red Cultural Mercosur surgió en 1998 como asociación civil sin fines
    de lucro. Está <strong>integrada por artistas, productores y gestores
    culturales reunidos bajo la convicción de que el trabajo en red constituye
    un concepto de gestión cultural que implica compartir información y
    capitalizar experiencias</strong>.</li>
    <li>Su objetivo es <strong>dinamizar la interacción geográfica y cultural de
    la Región</strong>, entendiendo a la creación artística y la realización de
    proyectos socio-culturales como un factor fundamental de integración en un
    territorio de 14.875.113 km² con una población total de 282.876.634
    habitantes.</li>
    <li>Cuenta con más de <strong>400 miembros individuales e
    institucionales</strong> de Argentina, Bolivia, Brasil, Chile, Paraguay,
    Uruguay y Venezuela.</li>
    <li>La Red Cultural del Mercosur <strong>articula y facilita la circulación
    regional de productos y servicios artísticos</strong>, la coproducción de
    <strong>eventos y espectáculos</strong>, la <strong>formación</strong> y
    <strong>especialización artística técnica</strong>, la
    <strong>profesionalización de la gestión cultural </strong>y la realización
    de <strong>proyectos artístico culturales</strong>.</li>
    <li>También forma parte de su misión <strong>estimular la formación y
    ampliación del público de las artes y la cultura</strong>, reforzando la
    construcción de una ciudadanía democrática en la que la inserción social se
    combine con la inserción cultural.</li>
    <li>Los <a href="<?php echo $this->url(array('page' => 'centros-del-sur.html'), 'base_static') ?>">corredores
    geográfico-culturales</a> <strong>son la herramienta elegida para crear circuitos
    alternativos de circulación de bienes culturales</strong> que favorecen la
    constitución de una geografía cultural regional más amplia que aquella que
    se constituye en el marco de los Estados-Nación.</li>
    <li>Los <strong>corredores geográfico-culturales presentan la producción de
    artistas provenientes de los distintos países de la Región en varias
    ciudades</strong>. Se proyecta la existencia de tres tipos de corredores:
    Nacionales (en ciudades que se encuentran dentro de las fronteras de cada
    país), Bilaterales (ciudades de dos países de la Región) y Multilaterales
    (en ciudades capitales o de equivalente envergadura de todos los países de
    la Región).</li>
    <li>La RCM <strong>está creando el Centro Virtual de Información Cultural
    del Mercosur de una base de datos dinámica de libre acceso</strong> (en
    español y portugués) que brinde información sobre los servicios y productos
    culturales existentes en las instituciones de la Red.</li>
    <li>El Centro Virtual de Información Cultural del Mercosur permitirá además
    <strong>visualizar información sobre las instituciones, sus recursos y
    servicios culturales vía Internet, a través de la página web de la
    RCM</strong>.</li>
</ol>

