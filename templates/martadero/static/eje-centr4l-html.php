<h2>¿Qué es Eje Central?</h2>
<p>La <strong>emprendeduría sostenible para el desarrollo cultural</strong>
(ESDC) busca combinar las herramientas procedentes de las prácticas de la
gestión cultural: planificación, evaluación, gestión en red, atención a la
diversidad y utilización de las TICs con la adopción de modelos y prácticas
procedentes de la creación de empresas y del fomento del desarrollo económico
local.</p>
<p>ESDC busca hacer viables en tres países (Bolivia, Perú, y Chile), proyectos
culturales existentes en territorios desfavorecidos durante 2010 y 2011. Para
ello <strong>se sirve internacionalmente de la formación y el trabajo en red,
mientras que localmente implica a grupos de acción cultural (GAC)</strong> que
ayuden a situar los proyectos culturales como un pilar del desarrollo local
sostenible.</p>
<p>La intervención se centra en la <strong>dotación de herramientas y actitudes
vinculadas a la emprendeduría a los responsables de proyecto de los países
citados</strong>. Con una metodología común, el proyecto se implementa en
diversos tipos de centro y territorio. El coordinador de la acción, ITD, cuenta
con una amplia experiencia en gestión de proyectos internacionales y se
encargará de su aplicación y seguimiento. En Bolivia, la acción coordinada por
Vanessa de Britto intervendrá en centros con una cierta trayectoria en
Cochabamba y La Paz; en Perú la acción coordinada por Tándem - Gestión Cultural
para el Desarrollo estará orientada a trabajar con centros Chimbote y en Villa
El Salvador; en Chile la coordinación realizada por Matucana100 orienta su
acción hacia centros en funcionamiento en Casablanca y Chillán.</p>

<h3>Acciones principales:</h3>
<ul>
    <li><strong>un encuentro internacional</strong> en que participen los
    coordinadores de cada país y los responsables de proyecto de los centros
    involucrados, en la que se presentará la metodología y se realizarán
    sesiones de formación específicas orientadas a emprendeduría y
    sostenibilidad de proyectos realizado los días 22 y 23 de marzo de 2010 en
    Santiago de Chile</li>
    <li>articulación de las <strong>microredes</strong> a nivel nacional entre
    el coordinador y los responsables de proyecto de cada centro implicado</li>
    <li>establecimiento de la <strong>red internacional</strong></li>
    <li>creación del blog <a href="http://www.emprendeduriacultural.net">www.emprendeduriacultural.net</a>
    como soporte y herramienta para comunicación y difusión de la red</li>
    <li>creación de <strong>planes de acción local</strong> por país y
    <strong>apoyo a la contratación</strong> de responsables de implementar el
    proyecto en cada centro</li>
    <li>creación de <strong>grupos de acción cultural</strong> local que
    promuevan la generación de actividades; programación de actividades como
    resultado de los GAC</li>
    <li>un <strong>workshop</strong> por país participante en el que se refuerce
    la micro red y se potencie la cooperación sur-sur</li>
    <li>definición de una <strong>estrategia de continuidad</strong> para el
    proyecto</li>
    <li>y una <strong>jornada final</strong> de presentación de resultados y
    difusión del proyecto.</li>
</ul>

