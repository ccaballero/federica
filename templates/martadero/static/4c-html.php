<h1>REDES CULTURALES<br />
4C - Circuito de Centros Culturales de Cochabamba</h1>

<p><img src="<?php echo $this->staticUrl('/marca-4c.jpg') ?>" class="box_image" />
<strong>Trabajar en red se presenta como una necesidad </strong>en el momento en
que vivimos. Se ha de dejar atrás la idea de "centro" estrella y ser conscientes
que es <strong>desde el trabajo interconectado y grupal como mejor conseguiremos
llevar a cabo nuestra misión</strong>.</p>

<p>El <strong>Circuito de Centros Culturales de Cochabamba</strong> nace en 2009
con esta finalidad, el de ofrecer una propuesta artística y cultural en conjunto
a la totalidad de la población cochabambina.</p>

<p><strong>Los centros</strong> que forman parte de esta iniciativa son:</p>

<p><strong>Proyecto mARTadero</strong>:
<a href="http://www.martadero.org">www.martadero.org</a></p>
<p><strong>Fundación Imagen</strong>:
<a href="http://www.fundacionimagen.org" target="_blank">www.fundacionimagen.org </a></p>
<p><strong>Centro Boliviano Americano</strong>:
<a href="http://www.cbacoch.org/" target="_blank">www.cbacoch.org/</a></p>
<p><strong>Alianza Francesa</strong>:
<a href="http://www.afbolivia.org/alianza-francesa-cochabamba.html" target="_blank">www.afbolivia.org</a></p>
<p><strong>Instituto Cultural Boliviano Alemán (ICBA)</strong>:
<a href="http://www.icbacbba.org/" target="_blank">www.icbacbba.org/</a></p>
<p><strong>Centro Pedagógico y Cultural Simón y Patiño</strong>:
<a href="http://centropatino.fundacionpatino.org/index.php" target="_blank">centropatino.fundacionpatino.org</a></p>

<p><strong>Además colaboran</strong>: La Fábrica, Museo de Historia Natural,
Casona Santivañez, Teatro Achá, Teatro Adela Zamudio, Jardín Botánico, Museo de
