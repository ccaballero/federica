<h1>REDES CULTURALES<br />
Mataderos del Mundo</h1>

<p>Actualmente existe una <strong>corriente mundial de rehabilitación de la
arquitectura industrial para usos culturales</strong>.</p>

<p>Los grandes equipamientos públicos del siglo XIX e inicios del XX muestran
características espaciales y una <strong>personalidad única para el arte
contemporáneo</strong> en sus diferentes expresiones. Diversas ciudades del
mundo están progresivamente recuperando usinas, mercados, fábricas, estaciones y
mataderos, redescubriendo y dándoles un uso creativo destinado al bien
social.</p>

<p>Desde 2004 el proyecto mARTadero va descubriendo y entrando en contacto con
diversos mataderos culturales del mundo, haciéndonos entender que <strong>no
estamos solos</strong>.</p>

