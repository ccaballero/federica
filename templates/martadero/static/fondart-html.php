<h1>Fondo para el Desarrollo de las Artes del mARTadero - FONDART</h1>

<p>El <strong>FONDART</strong>, Fondo para el Desarrollo de las Artes, es un
Fondo Concursable, alimentado por una tasa voluntaria del 10% sobre todos los
sueldos, productos y ganancias emanadas del proyecto, y que constituye el germen
de un fondo rotativo autogenerador de recursos. Podrá, además, contar con
aportes provenientes de donaciones, u otros fondos.</p>
<p><strong>Puedes descargarlo, consultarlo y comentarlo online desde</strong>
<a href="https://docs.google.com/document/d/1wFW-0Yaiyni8V1kPCYSlZ3LuCh0wh17k20ZgStu8HrI/edit" target="_blank">este link</a>.</p>

<h2>REGLAMENTO DEL FONDART</h2>
<p>Existen tres rangos de FONDART:</p>

<h3>FONDART PA (Pequeñas Ayudas): 0-400 Bs.</h3>
<p>Podrá ser solicitado mediante carta y debidamente aceptado y recibido
justificándose mediante aceptación firmada.</p>

<h4><em>Condiciones:</em></h4>
<ul>
    <li><em>La persona o grupo no podrá solicitarlo dos veces en el mismo
    semestre.</em></li>
    <li><em>La persona o grupo solicitante deberá poseer al menos una tarjeta
    ROSA. En el caso de grupos o artistas del exterior del departamento se podrá
    obviar este requisito.</em></li>
    <li><em>Se usará preferentemente para cubrir el aporte por uso del espacio,
    en el caso de haber contado con poca asistencia al evento (menos de 15
    personas).</em></li>
    <li><em>Podrá decidir su concesión el Nivel Coordinador, siguiendo las
    presentes condiciones</em></li>
</ul>

<h3>FONDART MA (Medianas Ayudas): 400-1600 Bs.</h3>
<p>Podrá ser solicitado mediante carta y debidamente aceptado y recibido
justificándose mediante aceptación firmada.</p>

<h4><em>Condiciones:</em></h4>
<ul>
    <li><em>La persona o grupo no podrá solicitarlo dos veces en el mismo
    año.</em></li>
    <li><em>Se usará preferentemente para cubrir necesidades del proyecto a
    realizarse que repercutan y/o queden para uso común como activo
    fijo.</em></li>
    <li><em>La persona o grupo solicitante deberá poseer al menos una tarjeta
    ROJA o ROSA. En el caso de grupos o artistas del exterior del departamento
    se podrá obviar este requisito bajo justificaciones fundamentadas.</em></li>
    <li><em>Se usará preferentemente para cubrir necesidades del proyecto a
    realizarse o de su difusión.</em></li>
    <li><em>Su solicitud será tomada en cuenta sólo en el caso de realizarse al
    menos con un mes de antelación.</em></li>
    <li><em>Podrá decidir su concesión el Nivel Estratégico, siguiendo las
    presentes condiciones.</em></li>
</ul>

<h3>FONDART GA (Grandes Ayudas). 1600-3200 Bs.</h3>
<p>Podrá ser solicitado mediante carta y debidamente aceptado y recibido
justificándose mediante aceptación firmada.</p>

<h4><em>Condiciones:</em></h4>
<ul>
    <li><em>La persona o grupo no podrá solicitarlo dos veces en el mismo
    año.</em></li>
    <li><em>Se usará preferentemente para cubrir necesidades del proyecto a
    realizarse que repercutan y/o queden para uso común como activo
    fijo.</em></li>
    <li><em>La persona o grupo solicitante deberá poseer al menos una tarjeta
    AZUL. En el caso de grupos o artistas del exterior del departamento se podrá
    obviar este requisito bajo justificaciones fundamentadas.</em></li>
    <li><em>Su solicitud será tomada en cuenta sólo en el caso de realizarse al
