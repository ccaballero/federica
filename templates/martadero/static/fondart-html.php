    Ofrecerá y justificará -además- una contrapartida para beneficio del
    proyecto.</em></li>
    <li><em>Podrá decidir su concesión el Nivel Organizacional (Directorio),
    siguiendo las </em><em>presentes condiciones.</em></li>
</ul>

<h2>Ejemplo de SOLICITUD DE FONDART</h2>
<h3>Pequeñas / Medianas / Grandes ayudas 2012</h3>

<p>El <strong>Nodo Asociativo para el Desarrollo de las Artes (NADA)</strong> es
una asociación artística, con personería jurídica, que pretende impulsar un
desarrollo integral de las artes a través de la promoción e interacción de los
artistas y eventos de diversas disciplinas, así como la conexión internacional a
través de arte y cultura de nuestra ciudad y país.</p>
<p>Para ello lleva a cabo el proyecto <strong>m<em>ART</em>adero, vivero de las
artes</strong>, como un megaproyecto integral de desarrollo artístico y cultural
con una estrategia basada en 3 pilares:</p>

<ul>
    <li><strong>el espacio</strong>: el conjunto arquitectónico ex-matadero:
    único, elocuente, flexible, descentralizado, estratégico social y
    geográficamente, acorde con la lógica y las necesidades de las artes
    emergentes; cedido en comodato por 30 años a NADA (Nodo Asociativo para el
    desarrollo de las artes) para el desarrollo autogestionario del
    proyecto.</li>
    <li><strong>la gestión cultural</strong>: un equipo profesional
    multidisciplinario y altamente cualificado, operante y con experiencia
    previa en el sector y en la promoción intercultural: en ámbitos locales,
    nacionales e internacionales.</li>
    <li><strong>los principios</strong>: innovación, investigación,
    experimentación, rigor conceptual y formal, integración, intercambio e
    interculturalidad, como criterios aplicables a toda propuesta generada,
    impulsada, apoyada u organizada por el proyecto.</li>
</ul>

<p>Así, el Nodo Asociativo para el Desarrollo de las Artes (N.A.D.A.) y el
Proyecto mARTadero, en cumplimiento de los principios con los cuales lleva
adelante su labor de Gestión Cultural -y en apoyo al desenvolvimiento de todos
aquellos grupos de teatro, promotores, actores y personas que llevan adelante la
actividad artística- ha creado un <strong>FONDO PARA EL DESARROLLO DE LAS ARTES
(FONDART)</strong>, que se genera con el 10% de todo el movimiento económico
generado en el proyecto, y que nos permite apoyar propuestas cuando así sea
necesario.</p>
<p>Por ello, y ante la solicitud cursada por los interesados, N.A.D.A. y el
Proyecto mARTadero han decidido beneficiar con el <strong>Fondart (en categoría
de Pequeñas/Medianas/Grandes Ayudas)</strong> al:</p>

<p><strong>GRUPO TEATRAL TURURU de Tupiza, Bolivia</strong></p>

<p>para así apoyarlo en el desarrollo que lleva adelante en <em>su difusión de
las tradiciones orales</em>, con un fondo de <strong>X.XXX Bs</strong>.</p>
<p>Dicho fondo se aplicará a <em>cubrir el costo del material para la
implementación de la señalética, compra de macetas, plantas, abono, pago de mano
de obra y materiales de albañilería de los diferentes proyectos</em> para el
beneficio del espacio y sus miembros.</p>

<p>Y para que así conste, firmamos en Cochabamba, 01 de Enero de 2012.</p>

