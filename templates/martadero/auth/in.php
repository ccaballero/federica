<h2><a name="requerimientos"></a>Requerimientos para solicitud de espacio</h2>
<p>Presentación de un proyecto artístico*, el cual contiene los siguientes
aspectos (<strong>éstos serán ingresados una vez iniciada sesión en nuestro
administrador digital y NO enviados por email</strong>):</p>

<ol>
    <li><strong>Nombre completo</strong> del solicitante, además del teléfono y
    dirección electrónica.</li>
    <li><strong>Titulo</strong> o nombre del Proyecto.</li>
    <li><strong>Fecha</strong> de producción.</li>
    <li><strong>Desarrollo del concepto o tema</strong> propuesto (o
    fundamentación conceptual).</li>
    <li><strong>Descripción  objetiva del proyecto</strong>, lo cual debe
    permitir imaginar el proyecto ya realizado y montado en su espacio
    respectivo. En este sentido se pueden agregar dibujos, fotografías,
    esquemas, videos, etc., y cualquier otro recurso que permita visualizar el
    proyecto terminado.</li>
    <li><strong>Requerimientos de montaje, técnicos y materiales </strong>(El
    propósito de esta solicitud no es necesariamente proveerlos, sino es tener
    una mayor comprensión del Proyecto).</li>
    <li><strong>Imágenes en alta y baja resolución</strong>, relativas al
    proyecto, que servirán para promocionar la exposición. (Éstas serán enviadas
    al mail del coordinador de área que puedes encontrar
    <a href="<?php echo $this->url(array('page' => 'contactos.html'), 'base_static') ?>#responsables-areas"
       target="_blank">pinchando aqui</a>)</li>
</ol>

<p><strong>*Un proyecto artístico</strong> es un plan de trabajo propuesto para
ser realizado en el ámbito de la percepción y la imagen, sobre la base de un
concepto o tema, concebido como resultado de una reflexión o reinterpretación
personal, por tanto original, acerca de un aspecto de la realidad.</p>

