<h1>PUEBLO</h1>
<p>Sea usted bienvenido, a la pagina de inicio del proyecto 'pueblo', sistema
web que ha pasado por mucha historia, nominación, y aventuras.</p>
<p>En lo respecta al autor de estas palabras, diremos que esto llego a su fin,
y que la naturaleza de este software, al fin, es definitiva.</p>
<p>Como notará, todo lo dicho anteriormente no ayuda en nada a saber cual es el
proposito de este sistema; bueno, diremos que el sistema en su etapa primigenia
no hace nada.</p>
<p>Y en esa gran nada que aqui presentamos, solo servimos tres páginas:</p>
<ul>
    <li><a href="<?php echo $this->url(array(), 'base') ?>">página de inicio (mi mismo)</a></li>
    <li><a href="<?php echo $this->url(array(), 'base_error') ?>">página de error</a></li>
    <li><a href="<?php echo $this->url(array(), 'base_confirm') ?>">página de confirmación</a></li>
</ul>
<p>En este mundo, donde las cosas duran un pestañeo, todos (incluso yo) se
preguntarán: ¿Cúal es la utilidad de un sistema que solo muestra tres páginas
absolutamente inutiles?</p>
<p>Pues bien, yo siempre digo:</p>
<blockquote>"todas las cosas son la misma cosa, y por tanto todas las cosas estan
reflejadas en las demas cosas"</blockquote>
<p>Este sistema es uno mas de mis intentos de demostrar esta teoria.</p>
